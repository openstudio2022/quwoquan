  static String _relativeIso({
    required int daysAgo,
    required int hour,
    required int minute,
  }) {
    final now = DateTime.now();
    final base = DateTime(
      now.year,
      now.month,
      now.day,
    ).subtract(Duration(days: daysAgo));
    return DateTime(
      base.year,
      base.month,
      base.day,
      hour,
      minute,
    ).toUtc().toIso8601String();
  }

