#!/usr/bin/env python3
"""Generate runtime/redis/default_prefix_routes.g.go from redis_keyspace.yaml.

Eliminates the hand-maintained duplicate in DefaultRouterConfig().
"""
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    print("pip install pyyaml", file=sys.stderr)
    sys.exit(1)

REPO = Path(__file__).resolve().parents[2]
KEYSPACE = REPO / "contracts" / "metadata" / "_shared" / "redis_keyspace.yaml"
OUTPUT = REPO / "runtime" / "redis" / "default_prefix_routes.g.go"

HEADER = '''// Code generated by scripts/codegen/gen_redis_router_config.py — DO NOT EDIT.

package redis
'''


def main():
    with open(KEYSPACE) as f:
        spec = yaml.safe_load(f)

    routing = spec.get("scene_routing", {})
    fallback = routing.get("fallback", "general")
    scenes = routing.get("scenes", {})

    # Collect all prefix -> scene mappings
    routes: list[tuple[str, str]] = []
    scene_names: list[str] = []
    for scene_name, scene_def in sorted(scenes.items()):
        scene_names.append(scene_name)
        for prefix in scene_def.get("key_prefixes", []):
            routes.append((prefix, scene_name))

    # Sort by prefix for deterministic output
    routes.sort(key=lambda r: r[0])

    lines = [HEADER]
    lines.append("// GeneratedPrefixRoutes returns prefix routes derived from redis_keyspace.yaml.")
    lines.append("func GeneratedPrefixRoutes() []PrefixRoute {")
    lines.append("\treturn []PrefixRoute{")
    for prefix, scene in routes:
        lines.append(f'\t\t{{Prefix: "{prefix}", Scene: "{scene}"}},')
    lines.append("\t}")
    lines.append("}")
    lines.append("")
    lines.append(f'// GeneratedDefaultScene is the fallback scene from redis_keyspace.yaml.')
    lines.append(f'const GeneratedDefaultScene = "{fallback}"')
    lines.append("")
    lines.append("// GeneratedSceneNames returns all scene names from redis_keyspace.yaml.")
    lines.append("func GeneratedSceneNames() []string {")
    lines.append("\treturn []string{")
    for name in sorted(scene_names):
        lines.append(f'\t\t"{name}",')
    lines.append("\t}")
    lines.append("}")
    lines.append("")

    output = "\n".join(lines)
    OUTPUT.write_text(output)
    print(f"Generated {OUTPUT}")


if __name__ == "__main__":
    main()
