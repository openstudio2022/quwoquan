func patchRepositoryImport(outputDir, packageName string) error {
	repoPath := filepath.Join(outputDir, "domain", packageName, "repository", "repository.go")
	data, err := os.ReadFile(repoPath)
	if err != nil {
		return err
	}
	raw := string(data)
	targetImport := fmt.Sprintf("quwoquan_service/services/content-service/internal/domain/%s/model", packageName)
	re := regexp.MustCompile(`model\s+"[^"]+"`)
	updated := re.ReplaceAllString(raw, fmt.Sprintf(`model "%s"`, targetImport))
	if updated == raw {
		return nil
	}
	formatted := []byte(updated)
	if out, err := format.Source(formatted); err == nil {
		formatted = out
	}
	return os.WriteFile(repoPath, formatted, 0o644)
}

// ── errors.yaml → Go errors.go ────────────────────────────────────────────────

type errorEntryYAML struct {
	Code      string `yaml:"code"`
	GoConst   string `yaml:"go_const"`
	Retryable bool   `yaml:"retryable"`
}

type errorsYAML struct {
	Domain string           `yaml:"domain"`
	Errors []errorEntryYAML `yaml:"errors"`
}

func generateErrorConstants(metadataDir, aggregate, outputDir string) error {
	// Support both legacy (post/) and domain-centric (content/post/) locations.
	aggLower := strings.ToLower(aggregate)
	errsPath := filepath.Join(metadataDir, aggLower, "errors.yaml")
	if _, err := os.Stat(errsPath); os.IsNotExist(err) {
		errsPath = filepath.Join(metadataDir, "content", aggLower, "errors.yaml")
	}
	data, err := os.ReadFile(errsPath)
	if err != nil {
		if os.IsNotExist(err) {
			return nil // errors.yaml is optional
		}
		return err
	}
	var spec errorsYAML
	if err := yaml.Unmarshal(data, &spec); err != nil {
		return fmt.Errorf("parse errors.yaml: %w", err)
	}
	if len(spec.Errors) == 0 {
		return nil
	}

	var buf bytes.Buffer
	buf.WriteString("// Code generated by tools/codegen_content_service. DO NOT EDIT.\n")
	buf.WriteString("package generated\n\n")
	buf.WriteString("import \"errors\"\n\n")
	buf.WriteString("// Content domain error sentinels bound to runtime/errors conventions.\n")
	buf.WriteString("//\n//nolint:gochecknoglobals\n")
	buf.WriteString("var (\n")
	sort.Slice(spec.Errors, func(i, j int) bool { return spec.Errors[i].GoConst < spec.Errors[j].GoConst })
	for _, e := range spec.Errors {
		if e.GoConst == "" {
			continue
		}
		buf.WriteString(fmt.Sprintf("\t%s = errors.New(%q)\n", e.GoConst, e.Code))
	}
	buf.WriteString(")\n")

	out := buf.Bytes()
	if formatted, err := format.Source(out); err == nil {
		out = formatted
	}
	targetDir := filepath.Join(outputDir, "generated")
	if err := os.MkdirAll(targetDir, 0o755); err != nil {
		return err
	}
	outPath := filepath.Join(targetDir, "errors.go")
	if err := os.WriteFile(outPath, out, 0o644); err != nil {
		return err
	}
	fmt.Printf("generated: %s\n", outPath)
	return nil
}

func exitErr(err error) {
	fmt.Fprintf(os.Stderr, "codegen_content_service error: %v\n", err)
	os.Exit(1)
}
