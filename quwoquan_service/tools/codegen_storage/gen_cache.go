package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"regexp"
	"strings"
	"text/template"

	"quwoquan_service/runtime/codegen"
)

type cacheData struct {
	EntityName   string
	CacheName    string
	KeyPattern   string
	KeyFunc      string
	KeyParamName string
	TTLSeconds   int
	CacheType    string
	ModelImport  string
	DomainPkg    string
}

var keyPlaceholderRE = regexp.MustCompile(`\{(\w+)\}`)

func generateCache(ctx *genContext, cache RedisCacheDef) error {
	// Check for skip override
	if override, ok := ctx.source.CacheOverrides[cache.Key]; ok {
		if override.Skip {
			fmt.Printf("  cache: skipped %s (skip override)\n", cache.Key)
			return nil
		}
	}

	if cache.Entity == "" {
		fmt.Printf("  cache: skipped %s (no entity, requires hand-written implementation)\n", cache.Key)
		return nil
	}

	dir := filepath.Join(ctx.outputDir(), "infrastructure", "cache")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	entity := cache.Entity
	cacheName := entity + "Cache"
	snakeEntity := codegen.CamelToSnake(entity)

	// Apply cache overrides for entity/name
	if override, ok := ctx.source.CacheOverrides[cache.Key]; ok {
		if override.Entity != "" {
			entity = override.Entity
		}
		if override.Name != "" {
			cacheName = override.Name + "Cache"
			snakeEntity = codegen.CamelToSnake(override.Name)
		}
	}

	keyFmt := keyPlaceholderRE.ReplaceAllString(cache.Key, "%s")
	matches := keyPlaceholderRE.FindAllStringSubmatch(cache.Key, -1)
	paramName := "id"
	if len(matches) > 0 {
		paramName = matches[0][1]
	}

	cacheType := cache.Type
	if cacheType == "" {
		cacheType = "json"
	}

	data := cacheData{
		EntityName:   entity,
		CacheName:    cacheName,
		KeyPattern:   cache.Key,
		KeyFunc:      snakeEntity + "Key",
		KeyParamName: paramName,
		TTLSeconds:   cache.TTLSeconds,
		CacheType:    cacheType,
		ModelImport:  ctx.modulePath() + "/domain/" + ctx.domainPkg() + "/model",
		DomainPkg:    ctx.domainPkg(),
	}

	var tmplStr string
	if cacheType == "set" {
		tmplStr = cacheSetTemplate
	} else {
		tmplStr = cacheJSONTemplate
	}

	tmpl, err := template.New("cache").Funcs(template.FuncMap{
		"keyFmt": func() string { return keyFmt },
	}).Parse(tmplStr)
	if err != nil {
		return fmt.Errorf("parse cache template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute cache template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		formatted = buf.Bytes()
	}

	fileName := snakeEntity + "_cache.g.go"
	path := filepath.Join(dir, fileName)
	fmt.Printf("  cache: %s\n", fileName)
	return os.WriteFile(path, formatted, 0644)
}

func init() {
	_ = strings.Join
}

const cacheJSONTemplate = `// Code generated by codegen_storage. DO NOT EDIT.
package cache

import (
	"context"
	"encoding/json"
	"fmt"
	"time"

	rtredis "quwoquan_service/runtime/redis"

	model "{{.ModelImport}}"
)

const {{.KeyFunc}}TTL = {{.TTLSeconds}} * time.Second

func {{.KeyFunc}}({{.KeyParamName}} string) string {
	return fmt.Sprintf("` + `{{keyFmt}}` + `", {{.KeyParamName}})
}

// {{.CacheName}} provides Redis caching for {{.EntityName}}.
type {{.CacheName}} struct {
	client rtredis.Client
}

// New{{.CacheName}} creates a new {{.CacheName}}.
func New{{.CacheName}}(client rtredis.Client) *{{.CacheName}} {
	return &{{.CacheName}}{client: client}
}

// Get retrieves a cached {{.EntityName}} by key parameter.
func (c *{{.CacheName}}) Get(ctx context.Context, {{.KeyParamName}} string) (*model.{{.EntityName}}, error) {
	data, err := c.client.Get(ctx, {{.KeyFunc}}({{.KeyParamName}}))
	if err != nil || data == "" {
		return nil, err
	}
	var entity model.{{.EntityName}}
	if err := json.Unmarshal([]byte(data), &entity); err != nil {
		return nil, err
	}
	return &entity, nil
}

// Set caches a {{.EntityName}} with the configured TTL.
func (c *{{.CacheName}}) Set(ctx context.Context, {{.KeyParamName}} string, entity *model.{{.EntityName}}) error {
	data, err := json.Marshal(entity)
	if err != nil {
		return err
	}
	return c.client.Set(ctx, {{.KeyFunc}}({{.KeyParamName}}), string(data), {{.KeyFunc}}TTL)
}

// Del removes the cached {{.EntityName}}.
func (c *{{.CacheName}}) Del(ctx context.Context, {{.KeyParamName}} string) error {
	return c.client.Del(ctx, {{.KeyFunc}}({{.KeyParamName}}))
}
`

