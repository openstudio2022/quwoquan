package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
	"text/template"

	"quwoquan_service/runtime/codegen"
)

type pgStoreData struct {
	TableName   string
	EntityName  string
	ShortName   string
	BaseName    string
	DomainPkg   string
	ModelImport string
	Columns     []pgColData
	PKColumn    string
	PKGoName    string
	FKColumn    string
	FKGoName    string
	HasFK       bool
	HasCreatedAt bool
	HasUpdatedAt bool
	NonPKCols   []pgColData
	NeedsTime   bool
}

type pgColData struct {
	DBName string
	GoName string
	IsPK   bool
}

func generatePGStore(ctx *genContext, tableName string, table TableDef) error {
	dir := filepath.Join(ctx.outputDir(), "infrastructure", "persistence")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	entity := table.Entity
	shortName := ctx.source.resolveStoreName(entity)
	snakeShort := codegen.CamelToSnake(shortName)

	data := pgStoreData{
		TableName:   tableName,
		EntityName:  entity,
		ShortName:   shortName,
		BaseName:    "pg" + shortName + "StoreBase",
		DomainPkg:   ctx.domainPkg(),
		ModelImport: ctx.modulePath() + "/domain/" + ctx.domainPkg() + "/model",
	}

	for _, col := range table.Columns {
		cd := pgColData{
			DBName: col.Name,
			GoName: codegen.SnakeToGoName(col.Name),
			IsPK:   col.IsPK(),
		}
		data.Columns = append(data.Columns, cd)

		if col.IsPK() {
			data.PKColumn = col.Name
			data.PKGoName = cd.GoName
		}
		if col.Name == "created_at" {
			data.HasCreatedAt = true
			data.NeedsTime = true
		}
		if col.Name == "updated_at" {
			data.HasUpdatedAt = true
			data.NeedsTime = true
		}
		if !col.IsPK() {
			data.NonPKCols = append(data.NonPKCols, cd)
		}
	}

	if table.FK != nil {
		data.HasFK = true
		data.FKColumn = table.FK.Column
		data.FKGoName = codegen.SnakeToGoName(table.FK.Column)
	}

	funcMap := template.FuncMap{
		"lowerFirst": codegen.PascalToLowerCamel,
		"colNames": func(cols []pgColData) string {
			names := make([]string, len(cols))
			for i, c := range cols {
				names[i] = c.DBName
			}
			return strings.Join(names, ", ")
		},
		"placeholders": func(cols []pgColData, offset int) string {
			ph := make([]string, len(cols))
			for i := range cols {
				ph[i] = fmt.Sprintf("$%d", i+1+offset)
			}
			return strings.Join(ph, ", ")
		},
		"scanFields": func(cols []pgColData, varName string) string {
			fields := make([]string, len(cols))
			for i, c := range cols {
				fields[i] = "&" + varName + "." + c.GoName
			}
			return strings.Join(fields, ", ")
		},
		"updateSet": func(cols []pgColData) string {
			parts := make([]string, len(cols))
			for i, c := range cols {
				parts[i] = fmt.Sprintf("%s=$%d", c.DBName, i+2)
			}
			return strings.Join(parts, ", ")
		},
		"createArgs": func(cols []pgColData, varName string) string {
			args := make([]string, len(cols))
			for i, c := range cols {
				args[i] = varName + "." + c.GoName
			}
			return strings.Join(args, ", ")
		},
	}

	tmpl, err := template.New("pg_store").Funcs(funcMap).Parse(pgStoreTemplate)
	if err != nil {
		return fmt.Errorf("parse pg template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute pg template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		formatted = buf.Bytes()
	}

	fileName := "pg_" + snakeShort + "_store.g.go"
	path := filepath.Join(dir, fileName)
	fmt.Printf("  pg_store: %s\n", fileName)
	return os.WriteFile(path, formatted, 0644)
}

