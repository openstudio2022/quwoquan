package main

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

func generateMigrationSQL(ctx *genContext, tableName string, table TableDef) error {
	dir := filepath.Join(ctx.outputDir(), "infrastructure", "migration")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	fileName := fmt.Sprintf("%03d_%s.up.sql", ctx.migrationSeq, tableName)
	path := filepath.Join(dir, fileName)

	var b strings.Builder
	b.WriteString("-- Code generated by codegen_storage. DO NOT EDIT.\n\n")

	needsTrgm := false
	for _, si := range table.SearchIndexes {
		if strings.Contains(strings.ToUpper(si.Type), "TRGM") {
			needsTrgm = true
			break
		}
	}
	if needsTrgm {
		b.WriteString("CREATE EXTENSION IF NOT EXISTS pg_trgm;\n\n")
	}

	b.WriteString(fmt.Sprintf("CREATE TABLE IF NOT EXISTS %s (\n", tableName))
	for i, col := range table.Columns {
		b.WriteString(fmt.Sprintf("    %-24s %s", col.Name, col.Type))

		if col.IsPK() {
			b.WriteString(" PRIMARY KEY")
		}
		if col.IsNotNull() && !col.IsPK() {
			b.WriteString(" NOT NULL")
		}
		if col.IsUnique() {
			b.WriteString(" UNIQUE")
		}
		if col.Default != nil {
			b.WriteString(fmt.Sprintf(" DEFAULT %v", col.Default))
		}

		if i < len(table.Columns)-1 {
			b.WriteString(",")
		}
		b.WriteString("\n")
	}

	if table.FK != nil {
		parts := strings.SplitN(table.FK.References, ".", 2)
		if len(parts) == 2 {
			onDelete := "CASCADE"
			if table.FK.OnDelete != "" {
				onDelete = strings.ToUpper(table.FK.OnDelete)
			}
			b.WriteString(fmt.Sprintf("    ,CONSTRAINT fk_%s_%s FOREIGN KEY (%s) REFERENCES %s(%s) ON DELETE %s\n",
				tableName, table.FK.Column, table.FK.Column, parts[0], parts[1], onDelete))
		}
	}

	b.WriteString(");\n")

	for _, idx := range table.Indexes {
		cols := strings.Join(idx.Columns, ", ")
		if idx.Unique {
			b.WriteString(fmt.Sprintf("\nCREATE UNIQUE INDEX IF NOT EXISTS %s ON %s (%s)", idx.Name, tableName, cols))
		} else {
			b.WriteString(fmt.Sprintf("\nCREATE INDEX IF NOT EXISTS %s ON %s (%s)", idx.Name, tableName, cols))
		}
		if idx.Condition != "" {
			b.WriteString(fmt.Sprintf(" WHERE %s", idx.Condition))
		}
		b.WriteString(";\n")
	}

	for _, uq := range table.UniqueConstraints {
		cols := strings.Join(uq.Columns, ", ")
		if uq.Condition != "" {
			b.WriteString(fmt.Sprintf("\nCREATE UNIQUE INDEX IF NOT EXISTS %s ON %s (%s) WHERE %s;\n",
				uq.Name, tableName, cols, uq.Condition))
		} else {
			b.WriteString(fmt.Sprintf("\nALTER TABLE %s ADD CONSTRAINT %s UNIQUE (%s);\n",
				tableName, uq.Name, cols))
		}
	}

	for _, si := range table.SearchIndexes {
		cols := strings.Join(si.Columns, ", ")
		switch strings.ToUpper(si.Type) {
		case "GIN_TRGM":
			ginCols := make([]string, len(si.Columns))
			for i, c := range si.Columns {
				ginCols[i] = c + " gin_trgm_ops"
			}
			b.WriteString(fmt.Sprintf("\nCREATE INDEX IF NOT EXISTS %s ON %s USING GIN (%s);\n",
				si.Name, tableName, strings.Join(ginCols, ", ")))
		default:
			b.WriteString(fmt.Sprintf("\nCREATE INDEX IF NOT EXISTS %s ON %s (%s);\n",
				si.Name, tableName, cols))
		}
	}

	fmt.Printf("  migration: %s\n", fileName)
	return os.WriteFile(path, []byte(b.String()), 0644)
}
