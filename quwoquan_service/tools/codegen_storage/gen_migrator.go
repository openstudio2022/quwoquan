package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"text/template"
)

func generateMigrator(manifest *Manifest) error {
	dir := filepath.Join(manifest.OutputDir, "infrastructure", "persistence")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	data := struct {
		ModulePath string
	}{
		ModulePath: manifest.ModulePath,
	}

	tmpl, err := template.New("migrator").Parse(migratorTemplate)
	if err != nil {
		return fmt.Errorf("parse migrator template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute migrator template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		formatted = buf.Bytes()
	}

	path := filepath.Join(dir, "migrator.g.go")
	fmt.Println("  migrator: migrator.g.go")
	return os.WriteFile(path, formatted, 0644)
}

const migratorTemplate = `// Code generated by codegen_storage. DO NOT EDIT.
package persistence

import (
	"context"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/jackc/pgx/v5/pgxpool"
)

// RunMigrations reads and executes all .up.sql files from the migration directory.
func RunMigrations(ctx context.Context, pool *pgxpool.Pool) error {
	migrationDir := findMigrationDir()
	if migrationDir == "" {
		return fmt.Errorf("migration directory not found")
	}

	entries, err := os.ReadDir(migrationDir)
	if err != nil {
		return fmt.Errorf("read migration dir: %w", err)
	}

	var files []string
	for _, e := range entries {
		if !e.IsDir() && strings.HasSuffix(e.Name(), ".up.sql") {
			files = append(files, e.Name())
		}
	}
	sort.Strings(files)

	for _, f := range files {
		sql, err := os.ReadFile(filepath.Join(migrationDir, f))
		if err != nil {
			return fmt.Errorf("read %s: %w", f, err)
		}
		if _, err := pool.Exec(ctx, string(sql)); err != nil {
			return fmt.Errorf("exec %s: %w", f, err)
		}
	}
	return nil
}

func findMigrationDir() string {
	candidates := []string{
		"internal/infrastructure/migration",
		filepath.Join("services", detectServiceName(), "internal", "infrastructure", "migration"),
	}
	for _, c := range candidates {
		if info, err := os.Stat(c); err == nil && info.IsDir() {
			return c
		}
	}
	return ""
}

func detectServiceName() string {
	exe, _ := os.Executable()
	return filepath.Base(filepath.Dir(filepath.Dir(exe)))
}
`
