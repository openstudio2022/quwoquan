package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

type assistantContractSchema struct {
	DartClass    string                                `yaml:"dart_class"`
	LibraryPath  string                                `yaml:"library_path"`
	OutputPath   string                                `yaml:"output_path"`
	Contract     string                                `yaml:"contract"`
	Version      string                                `yaml:"version"`
	Kind         string                                `yaml:"kind"`
	Imports      []string                              `yaml:"imports"`
	Fields       []assistantContractField              `yaml:"fields"`
	Subcontracts map[string]assistantSubcontractSchema `yaml:"subcontracts"`
}

type assistantSubcontractSchema struct {
	ClassName string                   `yaml:"class_name"`
	Fields    []assistantContractField `yaml:"fields"`
}

type assistantContractField struct {
	Name     string      `yaml:"name"`
	Type     string      `yaml:"type"`
	Required bool        `yaml:"required"`
	Default  interface{} `yaml:"default"`
	Ref      string      `yaml:"ref"`
	EnumRef  string      `yaml:"enum_ref"`
}

type assistantContractIndex struct {
	libraryByClass map[string]string
}

type assistantSchemaHeader struct {
	DartClass   string `yaml:"dart_class"`
	LibraryPath string `yaml:"library_path"`
}

func readAssistantContractSchema(path string) (*assistantContractSchema, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var parsed assistantContractSchema
	return &parsed, yaml.Unmarshal(data, &parsed)
}

func loadAssistantContractIndex(metadataDir string) (*assistantContractIndex, error) {
	baseDir := filepath.Join(metadataDir, "assistant")
	entries, err := os.ReadDir(baseDir)
	if err != nil {
		return nil, err
	}
	index := &assistantContractIndex{
		libraryByClass: map[string]string{},
	}
	for _, entry := range entries {
		if !entry.IsDir() || strings.HasPrefix(entry.Name(), "_") {
			continue
		}
		schemaPath := filepath.Join(baseDir, entry.Name(), "schema.yaml")
		data, err := os.ReadFile(schemaPath)
		if err != nil {
			if os.IsNotExist(err) {
				continue
			}
			return nil, err
		}
		var header assistantSchemaHeader
		if err := yaml.Unmarshal(data, &header); err != nil {
			return nil, err
		}
		if strings.TrimSpace(header.DartClass) == "" || strings.TrimSpace(header.LibraryPath) == "" {
			continue
		}
		index.libraryByClass[strings.TrimSpace(header.DartClass)] = strings.TrimSpace(header.LibraryPath)
	}
	return index, nil
}

func renderAssistantSchemaDrivenContract(schema *assistantContractSchema, index *assistantContractIndex, sourceMeta string) string {
	var b strings.Builder
	b.WriteString(fmt.Sprintf("// Code generated by tools/codegen_app_metadata from %s. DO NOT EDIT.\n\n", sourceMeta))
	b.WriteString("// ignore_for_file: avoid_classes_with_only_static_members\n\n")

	imports := assistantCollectContractImports(schema, index)
	if len(imports) > 0 {
		for _, imp := range imports {
			b.WriteString(fmt.Sprintf("import '%s';\n", imp))
		}
		b.WriteString("\n")
	}

	subcontractKeys := assistantReferencedLocalSubcontracts(schema)
	for _, key := range subcontractKeys {
		sub := schema.Subcontracts[key]
		b.WriteString(renderAssistantSchemaClass(sub.ClassName, sub.Fields, schema, index))
		b.WriteString("\n")
		b.WriteString(renderAssistantSchemaFieldConstants(sub.ClassName, sub.Fields))
		b.WriteString("\n")
	}

	b.WriteString(renderAssistantSchemaClass(schema.DartClass, schema.Fields, schema, index))
	b.WriteString("\n")
	b.WriteString(renderAssistantSchemaFieldConstants(schema.DartClass, schema.Fields))
	return b.String()
}

func assistantReferencedLocalSubcontracts(schema *assistantContractSchema) []string {
	seen := map[string]bool{}
	var keys []string
	var visitFields func([]assistantContractField)
	visitFields = func(fields []assistantContractField) {
		for _, field := range fields {
			if field.Ref == "" {
				continue
			}
			sub, ok := schema.Subcontracts[field.Ref]
			if !ok || seen[field.Ref] {
				continue
			}
			seen[field.Ref] = true
			keys = append(keys, field.Ref)
			visitFields(sub.Fields)
		}
	}
	visitFields(schema.Fields)
	sort.Strings(keys)
	return keys
}

func assistantCollectContractImports(schema *assistantContractSchema, index *assistantContractIndex) []string {
	importsSet := map[string]bool{}
	for _, imp := range schema.Imports {
		trimmed := strings.TrimSpace(imp)
		if trimmed != "" {
			importsSet[trimmed] = true
		}
	}
	if assistantSchemaNeedsRuntimeEnums(schema) &&
		!importsSet["package:quwoquan_app/assistant/contracts/assistant_turn_contract.dart"] &&
		!importsSet["package:quwoquan_app/assistant/contracts/context_fill_contract.dart"] {
		importsSet["package:quwoquan_app/assistant/contracts/runtime_enums.dart"] = true
	}
	for _, field := range schema.Fields {
		ref := strings.TrimSpace(field.Ref)
		if ref == "" {
			continue
		}
		if _, ok := schema.Subcontracts[ref]; ok {
			continue
		}
		if libraryPath, ok := index.libraryByClass[ref]; ok && libraryPath != schema.LibraryPath {
			importsSet["package:quwoquan_app/"+libraryPath] = true
		}
	}
	var imports []string
	for imp := range importsSet {
		imports = append(imports, imp)
	}
	sort.Strings(imports)
	return imports
}

func assistantSchemaNeedsRuntimeEnums(schema *assistantContractSchema) bool {
	for _, field := range schema.Fields {
		if strings.TrimSpace(field.EnumRef) != "" {
			return true
		}
	}
	for _, key := range assistantReferencedLocalSubcontracts(schema) {
		for _, field := range schema.Subcontracts[key].Fields {
			if strings.TrimSpace(field.EnumRef) != "" {
				return true
			}
		}
	}
	return false
}

func renderAssistantSchemaClass(
	className string,
	fields []assistantContractField,
	schema *assistantContractSchema,
	index *assistantContractIndex,
) string {
	var b strings.Builder
	b.WriteString(fmt.Sprintf("class %s {\n", className))
	b.WriteString(fmt.Sprintf("  const %s({\n", className))
	for _, field := range fields {
		decl := assistantRenderConstructorField(field, schema, index)
		b.WriteString("    " + decl + "\n")
	}
	b.WriteString("  });\n\n")
	for _, field := range fields {
		b.WriteString(fmt.Sprintf("  final %s %s;\n", assistantResolveFieldDartType(field, schema, index), field.Name))
	}
	b.WriteString("\n")
	b.WriteString("  Map<String, dynamic> toJson() => <String, dynamic>{\n")
	for _, field := range fields {
		b.WriteString(fmt.Sprintf("        '%s': %s,\n", field.Name, assistantRenderToJsonValue("        ", field, field.Name, schema, index)))
	}
	b.WriteString("      };\n\n")
	b.WriteString(fmt.Sprintf("  factory %s.fromJson(Map<String, dynamic> json) {\n", className))
	b.WriteString(fmt.Sprintf("    return %s(\n", className))
	for _, field := range fields {
		b.WriteString(fmt.Sprintf("      %s: %s,\n", field.Name, assistantRenderFromJsonValue(field, schema, index)))
	}
	b.WriteString("    );\n")
	b.WriteString("  }\n")
	if assistantClassNeedsHelpers(fields) {
		b.WriteString("\n")
		b.WriteString(assistantRenderSchemaHelpers(fields, schema, index))
	}
	b.WriteString("}\n")
	return b.String()
}

func renderAssistantSchemaFieldConstants(
	className string,
	fields []assistantContractField,
) string {
	var b strings.Builder
	b.WriteString(fmt.Sprintf("class %sFields {\n", className))
	for _, field := range fields {
		b.WriteString(fmt.Sprintf("  static const String %s = '%s';\n", field.Name, field.Name))
	}
	b.WriteString("}\n")
	return b.String()
}

func assistantRenderConstructorField(field assistantContractField, schema *assistantContractSchema, index *assistantContractIndex) string {
	if field.Required && field.Default == nil {
		return fmt.Sprintf("required this.%s,", field.Name)
	}
	if field.Default != nil {
		return fmt.Sprintf("this.%s = %s,", field.Name, assistantRenderDefaultValue(field, schema, index))
	}
	return fmt.Sprintf("this.%s,", field.Name)
}

func assistantResolveFieldDartType(field assistantContractField, schema *assistantContractSchema, index *assistantContractIndex) string {
	baseType := assistantResolveBaseDartType(field, schema, index)
	if field.Required || field.Default != nil || assistantFieldIsCollection(field) || assistantFieldIsPrimitive(field) || field.Type == "enum" {
		return baseType
	}
	if field.Type == "object" && field.Ref != "" {
		return baseType + "?"
	}
	if field.Type == "datetime" {
		return "DateTime?"
	}
	return baseType
}

func assistantResolveBaseDartType(field assistantContractField, schema *assistantContractSchema, index *assistantContractIndex) string {
	switch field.Type {
	case "string":
		return "String"
	case "int":
		return "int"
	case "double":
		return "double"
	case "bool":
		return "bool"
	case "datetime":
		return "DateTime"
	case "map":
		return "Map<String, dynamic>"
	case "list<string>":
		return "List<String>"
	case "list<map>":
		return "List<Map<String, dynamic>>"
	case "enum":
		return field.EnumRef
	case "object":
		if field.Ref != "" {
			return assistantResolveRefClassName(field.Ref, schema, index)
		}
		return "Map<String, dynamic>"
	case "list<object>":
		if field.Ref != "" {
			return "List<" + assistantResolveRefClassName(field.Ref, schema, index) + ">"
		}
		return "List<Map<String, dynamic>>"
	case "map<object>":
		if field.Ref != "" {
			return "Map<String, " + assistantResolveRefClassName(field.Ref, schema, index) + ">"
		}
		return "Map<String, Map<String, dynamic>>"
	case "any":
		return "dynamic"
	default:
		return "dynamic"
	}
}

func assistantResolveRefClassName(ref string, schema *assistantContractSchema, index *assistantContractIndex) string {
	if sub, ok := schema.Subcontracts[ref]; ok && strings.TrimSpace(sub.ClassName) != "" {
		return strings.TrimSpace(sub.ClassName)
	}
	return strings.TrimSpace(ref)
}

func assistantRenderDefaultValue(field assistantContractField, schema *assistantContractSchema, index *assistantContractIndex) string {
	switch field.Type {
	case "string":
		return fmt.Sprintf("%q", assistantDefaultString(field.Default))
	case "int":
		return fmt.Sprintf("%d", assistantDefaultInt(field.Default))
	case "double":
		return assistantFormatFloat(assistantDefaultFloat(field.Default))
	case "bool":
		if assistantDefaultBool(field.Default) {
			return "true"
		}
		return "false"
	case "datetime":
		return "null"
	case "map":
		return "const <String, dynamic>{}"
	case "list<string>":
		return "const <String>[]"
	case "list<map>":
		return "const <Map<String, dynamic>>[]"
	case "enum":
		return assistantRenderEnumDefaultValue(field)
	case "object":
		if field.Ref != "" {
			return fmt.Sprintf("const %s()", assistantResolveRefClassName(field.Ref, schema, index))
		}
		return "const <String, dynamic>{}"
	case "list<object>":
		if field.Ref != "" {
			return fmt.Sprintf("const <%s>[]", assistantResolveRefClassName(field.Ref, schema, index))
		}
		return "const <Map<String, dynamic>>[]"
	case "map<object>":
		if field.Ref != "" {
			return fmt.Sprintf("const <String, %s>{}", assistantResolveRefClassName(field.Ref, schema, index))
		}
		return "const <String, Map<String, dynamic>>{}"
	case "any":
		return "null"
	default:
		return "null"
	}
}

func assistantRenderToJsonValue(indent string, field assistantContractField, accessor string, schema *assistantContractSchema, index *assistantContractIndex) string {
	switch field.Type {
	case "enum":
		return accessor + ".wireName"
	case "object":
		if field.Ref != "" {
			if strings.HasSuffix(assistantResolveFieldDartType(field, schema, index), "?") {
				return accessor + "?.toJson()"
			}
			return accessor + ".toJson()"
		}
		return accessor
	case "list<object>":
		if field.Ref != "" {
			return accessor + ".map((item) => item.toJson()).toList(growable: false)"
		}
		return accessor
	case "map<object>":
		if field.Ref != "" {
			return fmt.Sprintf("<String, dynamic>{\n%s  for (final entry in %s.entries) entry.key: entry.value.toJson(),\n%s}", indent, accessor, indent)
		}
		return accessor
	case "datetime":
		return accessor + "?.toIso8601String()"
	default:
		return accessor
	}
}

func assistantRenderFromJsonValue(field assistantContractField, schema *assistantContractSchema, index *assistantContractIndex) string {
	jsonAccessor := fmt.Sprintf("json['%s']", field.Name)
	switch field.Type {
	case "string":
		return fmt.Sprintf("(json['%s'] as String?)?.trim() ?? %s", field.Name, assistantRenderDefaultValue(field, schema, index))
	case "int":
		return fmt.Sprintf("(json['%s'] as num?)?.toInt() ?? %d", field.Name, assistantDefaultInt(field.Default))
	case "double":
		return fmt.Sprintf("(json['%s'] as num?)?.toDouble() ?? %s", field.Name, assistantFormatFloat(assistantDefaultFloat(field.Default)))
	case "bool":
		if assistantDefaultBool(field.Default) {
			return fmt.Sprintf("json['%s'] != false", field.Name)
		}
		return fmt.Sprintf("json['%s'] == true", field.Name)
	case "datetime":
		return fmt.Sprintf("((json['%s'] as String?)?.trim().isNotEmpty == true) ? DateTime.tryParse((json['%s'] as String).trim()) : null", field.Name, field.Name)
	case "map":
		return fmt.Sprintf("(json['%s'] as Map?)?.cast<String, dynamic>() ?? const <String, dynamic>{}", field.Name)
	case "list<string>":
		return fmt.Sprintf("_assistantStringList(json['%s'])", field.Name)
	case "list<map>":
		return fmt.Sprintf("_assistantMapList(json['%s'])", field.Name)
	case "enum":
		return fmt.Sprintf("parse%s((json['%s'] as String?)?.trim() ?? %q)", field.EnumRef, field.Name, assistantDefaultString(field.Default))
	case "object":
		if field.Ref != "" {
			className := assistantResolveRefClassName(field.Ref, schema, index)
			if field.Default != nil {
				return fmt.Sprintf("%s is Map ? %s.fromJson((%s as Map).cast<String, dynamic>()) : const %s()", jsonAccessor, className, jsonAccessor, className)
			}
			return fmt.Sprintf("%s is Map ? %s.fromJson((%s as Map).cast<String, dynamic>()) : null", jsonAccessor, className, jsonAccessor)
		}
		return fmt.Sprintf("(json['%s'] as Map?)?.cast<String, dynamic>() ?? const <String, dynamic>{}", field.Name)
	case "list<object>":
		if field.Ref != "" {
			className := assistantResolveRefClassName(field.Ref, schema, index)
			return fmt.Sprintf("(json['%s'] as List?)?.whereType<Map>().map((item) => %s.fromJson(item.cast<String, dynamic>())).toList(growable: false) ?? %s", field.Name, className, assistantRenderDefaultValue(field, schema, index))
		}
		return fmt.Sprintf("_assistantMapList(json['%s'])", field.Name)
	case "map<object>":
		className := assistantResolveRefClassName(field.Ref, schema, index)
		return fmt.Sprintf("_assistantObjectMap(json['%s'], (key, value) => %s.fromJson(value))", field.Name, className)
	case "any":
		return fmt.Sprintf("json['%s']", field.Name)
	default:
		return jsonAccessor
	}
}

func assistantClassNeedsHelpers(fields []assistantContractField) bool {
	for _, field := range fields {
		switch field.Type {
		case "list<string>", "list<map>", "map<object>":
			return true
		case "object":
			return true
		}
	}
	return false
}

func assistantRenderSchemaHelpers(fields []assistantContractField, schema *assistantContractSchema, index *assistantContractIndex) string {
	var b strings.Builder
	needsStringList := false
	needsMapList := false
	needsObjectMap := false
	for _, field := range fields {
		switch field.Type {
		case "list<string>":
			needsStringList = true
		case "list<map>":
			needsMapList = true
		case "map<object>":
			needsObjectMap = true
		case "object":
			if field.Ref != "" {
				_ = assistantResolveRefClassName(field.Ref, schema, index)
			}
		}
	}
	if needsStringList {
		b.WriteString("  static List<String> _assistantStringList(Object? value) {\n")
		b.WriteString("    if (value is List) {\n")
		b.WriteString("      return value.map((item) => item.toString().trim()).where((item) => item.isNotEmpty).toList(growable: false);\n")
		b.WriteString("    }\n")
		b.WriteString("    return const <String>[];\n")
		b.WriteString("  }\n")
		if needsMapList || needsObjectMap {
			b.WriteString("\n")
		}
	}
	if needsMapList {
		b.WriteString("  static List<Map<String, dynamic>> _assistantMapList(Object? value) {\n")
		b.WriteString("    if (value is List) {\n")
		b.WriteString("      return value.whereType<Map>().map((item) => item.cast<String, dynamic>()).toList(growable: false);\n")
		b.WriteString("    }\n")
		b.WriteString("    return const <Map<String, dynamic>>[];\n")
		b.WriteString("  }\n")
		if needsObjectMap {
			b.WriteString("\n")
		}
	}
	if needsObjectMap {
		b.WriteString("  static Map<String, T> _assistantObjectMap<T>(\n")
		b.WriteString("    Object? value,\n")
		b.WriteString("    T Function(String key, Map<String, dynamic> value) parser,\n")
		b.WriteString("  ) {\n")
		b.WriteString("    if (value is! Map) return <String, T>{};\n")
		b.WriteString("    final typed = <String, T>{};\n")
		b.WriteString("    for (final entry in value.entries) {\n")
		b.WriteString("      final raw = entry.value;\n")
		b.WriteString("      if (raw is! Map) continue;\n")
		b.WriteString("      typed[entry.key.toString()] = parser(entry.key.toString(), raw.cast<String, dynamic>());\n")
		b.WriteString("    }\n")
		b.WriteString("    return typed;\n")
		b.WriteString("  }\n")
	}
	return b.String()
}

func assistantFieldIsCollection(field assistantContractField) bool {
	return strings.HasPrefix(field.Type, "list<") || strings.HasPrefix(field.Type, "map")
}

func assistantFieldIsPrimitive(field assistantContractField) bool {
	switch field.Type {
	case "string", "int", "double", "bool", "any":
		return true
	default:
		return false
	}
}

func assistantDefaultString(value interface{}) string {
	if value == nil {
		return ""
	}
	switch typed := value.(type) {
	case string:
		return typed
	default:
		return fmt.Sprint(typed)
	}
}

func assistantDefaultInt(value interface{}) int {
	if value == nil {
		return 0
	}
	switch typed := value.(type) {
	case int:
		return typed
	case int64:
		return int(typed)
	case float64:
		return int(typed)
	default:
		return 0
	}
}

func assistantDefaultFloat(value interface{}) float64 {
	if value == nil {
		return 0
	}
	switch typed := value.(type) {
	case float64:
		return typed
	case int:
		return float64(typed)
	case int64:
		return float64(typed)
	default:
		return 0
	}
}

func assistantDefaultBool(value interface{}) bool {
	if value == nil {
		return false
	}
	typed, ok := value.(bool)
	return ok && typed
}

func assistantFormatFloat(value float64) string {
	return strings.TrimRight(strings.TrimRight(fmt.Sprintf("%.6f", value), "0"), ".")
}

func assistantRenderEnumDefaultValue(field assistantContractField) string {
	return fmt.Sprintf("%s.%s", field.EnumRef, assistantEnumMemberNameFromDefault(field.EnumRef, assistantDefaultString(field.Default)))
}

func assistantEnumMemberNameFromDefault(enumName, raw string) string {
	trimmed := strings.TrimSpace(raw)
	if trimmed == "" {
		return assistantEnumDefault(enumName)
	}
	parts := strings.FieldsFunc(trimmed, func(r rune) bool {
		return r == '_' || r == '-' || r == ' '
	})
	if len(parts) == 0 {
		return assistantEnumDefault(enumName)
	}
	var b strings.Builder
	for index, part := range parts {
		if part == "" {
			continue
		}
		lower := strings.ToLower(part[:1]) + part[1:]
		if index == 0 {
			b.WriteString(lower)
			continue
		}
		b.WriteString(strings.ToUpper(lower[:1]) + lower[1:])
	}
	result := b.String()
	if result == "" {
		return assistantEnumDefault(enumName)
	}
	return result
}
