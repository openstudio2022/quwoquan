// generateAssistantWireGoPoC emits minimal Go structs from assistant run_artifacts + assistant_turn
// schema.yaml for cloud-side wire PoC (json tags align with Dart toJson keys).
func generateAssistantWireGoPoC(metadataDir, serviceRoot string) error {
	base := filepath.Join(metadataDir, "assistant")
	raPath := filepath.Join(base, "run_artifacts", "schema.yaml")
	atPath := filepath.Join(base, "assistant_turn", "schema.yaml")
	srwPath := filepath.Join(base, "assistant_structured_response_wire", "schema.yaml")

	ra, err := readAssistantContractSchema(raPath)
	if err != nil {
		return fmt.Errorf("assistant wire go: run_artifacts: %w", err)
	}
	at, err := readAssistantContractSchema(atPath)
	if err != nil {
		return fmt.Errorf("assistant wire go: assistant_turn: %w", err)
	}
	srw, err := readAssistantContractSchema(srwPath)
	if err != nil {
		return fmt.Errorf("assistant wire go: assistant_structured_response_wire: %w", err)
	}

	outRA := filepath.Join(serviceRoot, "generated", "assistant", "wirepoc", "runartifacts", "run_artifacts_wire.go")
	outAT := filepath.Join(serviceRoot, "generated", "assistant", "wirepoc", "assistantturn", "assistant_turn_wire.go")
	outSRW := filepath.Join(serviceRoot, "generated", "assistant", "wirepoc", "structuredresponsewire", "structured_response_wire.go")

	if err := os.MkdirAll(filepath.Dir(outRA), 0o755); err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(outAT), 0o755); err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(outSRW), 0o755); err != nil {
		return err
	}

	writeFile(outRA, renderAssistantGoWirePackage("runartifacts", ra, "run_artifacts/schema.yaml"))
	writeFile(outAT, renderAssistantGoWirePackage("assistantturn", at, "assistant_turn/schema.yaml"))
	writeFile(outSRW, renderAssistantGoWirePackage("structuredresponsewire", srw, "assistant_structured_response_wire/schema.yaml"))
	return nil
}

func renderAssistantGoWirePackage(pkg string, schema *assistantContractSchema, sourceMeta string) string {
	order := assistantGoWireSubcontractOrder(schema)
	var b strings.Builder
	b.WriteString("// Code generated by tools/codegen_app_metadata (assistant wire Go PoC) from assistant/")
	b.WriteString(sourceMeta)
	b.WriteString(". DO NOT EDIT.\n\n")
	b.WriteString("package ")
	b.WriteString(pkg)
	b.WriteString("\n\nimport \"encoding/json\"\n\n")

	for _, key := range order {
		sub := schema.Subcontracts[key]
		if strings.TrimSpace(sub.ClassName) == "" {
			continue
		}
		assistantGoWireEmitStruct(&b, schema, sub.ClassName, sub.Fields)
		b.WriteString("\n")
	}
	root := strings.TrimSpace(schema.DartClass)
	if root != "" {
		assistantGoWireEmitStruct(&b, schema, root, schema.Fields)
	}
	return b.String()
}

func assistantGoWireSubcontractOrder(schema *assistantContractSchema) []string {
	keys := make([]string, 0, len(schema.Subcontracts))
	for k := range schema.Subcontracts {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	seen := map[string]bool{}
	var order []string
	var visit func(k string)
	visit = func(k string) {
		if seen[k] {
			return
		}
		seen[k] = true
		sub, ok := schema.Subcontracts[k]
		if !ok {
			return
		}
		for _, f := range sub.Fields {
			ref := strings.TrimSpace(f.Ref)
			if _, ok := schema.Subcontracts[ref]; ok {
				visit(ref)
			}
		}
		order = append(order, k)
	}
	for _, k := range keys {
		visit(k)
	}
	return order
}

func assistantGoWireEmitStruct(b *strings.Builder, schema *assistantContractSchema, goName string, fields []assistantContractField) {
	fmt.Fprintf(b, "type %s struct {\n", goName)
	for _, f := range fields {
		jsonTag := f.Name
		goField := assistantGoExportIdent(f.Name)
		goTyp, omit := assistantGoWireFieldType(schema, f)
		tag := jsonTag
		if omit {
			tag = jsonTag + ",omitempty"
		}
		fmt.Fprintf(b, "\t%s %s `json:\"%s\"`\n", goField, goTyp, tag)
	}
	b.WriteString("}\n")
}

func assistantGoExportIdent(name string) string {
	if name == "" {
		return name
	}
	// Minimal camelCase → exported Go: upper first rune only (PoC).
	r := []rune(name)
	if len(r) == 0 {
		return name
	}
	return strings.ToUpper(string(r[0])) + string(r[1:])
}

func assistantGoWireFieldType(schema *assistantContractSchema, f assistantContractField) (typ string, omitempty bool) {
	opt := assistantGoWireOptional(f)
	switch strings.TrimSpace(f.Type) {
	case "string":
		if opt {
			return "*string", true
		}
		return "string", false
	case "int":
		if opt {
			return "*int", true
		}
		return "int", false
	case "double":
		if opt {
			return "*float64", true
		}
		return "float64", false
	case "bool":
		return "bool", false
	case "enum":
		if opt {
			return "*string", true
		}
		return "string", false
	case "map", "any":
		return "json.RawMessage", opt
	case "list<string>":
		return "[]string", opt
	case "list<map>":
		return "[]json.RawMessage", opt
	case "object":
		ref := strings.TrimSpace(f.Ref)
		if ref != "" {
			if sub, ok := schema.Subcontracts[ref]; ok && strings.TrimSpace(sub.ClassName) != "" {
				cn := strings.TrimSpace(sub.ClassName)
				return "*" + cn, opt
			}
		}
		return "json.RawMessage", opt
	case "list<object>":
		ref := strings.TrimSpace(f.Ref)
		if ref != "" {
			if sub, ok := schema.Subcontracts[ref]; ok && strings.TrimSpace(sub.ClassName) != "" {
				return "[]" + strings.TrimSpace(sub.ClassName), opt
			}
		}
		return "[]json.RawMessage", opt
	case "map<object>":
		return "json.RawMessage", opt
	default:
		return "json.RawMessage", true
	}
}

func assistantGoWireOptional(f assistantContractField) bool {
	if f.Required {
		return false
	}
	return f.Default == nil
}
