// generateAssistantWireGoPoC emits minimal Go structs from assistant run_artifacts + assistant_turn
// schema.yaml for cloud-side wire PoC (json tags align with Dart toJson keys).
func generateAssistantWireGoPoC(metadataDir, serviceRoot string) error {
	base := filepath.Join(metadataDir, "assistant")
	raPath := filepath.Join(base, "run_artifacts", "schema.yaml")
	atPath := filepath.Join(base, "assistant_turn", "schema.yaml")
	srwPath := filepath.Join(base, "assistant_structured_response_wire", "schema.yaml")

	ra, err := readAssistantContractSchema(raPath)
	if err != nil {
		return fmt.Errorf("assistant wire go: run_artifacts: %w", err)
	}
	at, err := readAssistantContractSchema(atPath)
	if err != nil {
		return fmt.Errorf("assistant wire go: assistant_turn: %w", err)
	}
	srw, err := readAssistantContractSchema(srwPath)
	if err != nil {
		return fmt.Errorf("assistant wire go: assistant_structured_response_wire: %w", err)
	}

	outRA := filepath.Join(serviceRoot, "generated", "assistant", "wirepoc", "runartifacts", "run_artifacts_wire.go")
	outAT := filepath.Join(serviceRoot, "generated", "assistant", "wirepoc", "assistantturn", "assistant_turn_wire.go")
	outSRW := filepath.Join(serviceRoot, "generated", "assistant", "wirepoc", "structuredresponsewire", "structured_response_wire.go")

	if err := os.MkdirAll(filepath.Dir(outRA), 0o755); err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(outAT), 0o755); err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(outSRW), 0o755); err != nil {
		return err
	}

	writeFile(outRA, renderAssistantGoWirePackage("runartifacts", ra, "assistant/run_artifacts/schema.yaml"))
	writeFile(outAT, renderAssistantGoWirePackage("assistantturn", at, "assistant/assistant_turn/schema.yaml"))
	writeFile(outSRW, renderAssistantGoWirePackage("structuredresponsewire", srw, "assistant/assistant_structured_response_wire/schema.yaml"))
	m2Schemas := []struct {
		domain  string
		name    string
		pkg     string
		fixture string
	}{
		{domain: "assistant", name: "runtime_failure", pkg: "runtimefailure", fixture: "m2_runtime_failure_min.json"},
		{domain: "assistant", name: "assistant_conversation", pkg: "assistantconversation", fixture: "m2_assistant_conversation_min.json"},
		{domain: "assistant", name: "assistant_turn_envelope", pkg: "assistantturnenvelope", fixture: "m2_assistant_turn_envelope_min.json"},
		{domain: "assistant", name: "skill_subscription", pkg: "skillsubscription", fixture: "m2_skill_subscription_min.json"},
		{domain: "assistant", name: "device_context", pkg: "devicecontext", fixture: "m2_device_context_min.json"},
		{domain: "assistant", name: "tool_use", pkg: "tooluse", fixture: "m2_tool_use_min.json"},
		{domain: "assistant", name: "assistant_stream_event", pkg: "assistantstreamevent", fixture: "m2_assistant_stream_event_min.json"},
		{domain: "notification", name: "app_message", pkg: "appmessage", fixture: "m2_app_message_min.json"},
	}
	for _, item := range m2Schemas {
		schemaPath := filepath.Join(metadataDir, item.domain, item.name, "schema.yaml")
		schema, err := readAssistantContractSchema(schemaPath)
		if err != nil {
			if os.IsNotExist(err) {
				continue
			}
			return fmt.Errorf("assistant wire go: %s/%s: %w", item.domain, item.name, err)
		}
		outDir := filepath.Join(serviceRoot, "generated", "assistant", "wirepoc", item.pkg)
		if err := os.MkdirAll(outDir, 0o755); err != nil {
			return err
		}
		source := filepath.Join(item.domain, item.name, "schema.yaml")
		writeFile(filepath.Join(outDir, item.name+"_wire.go"), renderAssistantGoWirePackage(item.pkg, schema, source))
		writeFile(filepath.Join(outDir, "wire_roundtrip_test.go"), renderAssistantGoWireRoundTripTest(item.pkg, schema, item.fixture))
	}
	return nil
}

func renderAssistantGoWirePackage(pkg string, schema *assistantContractSchema, sourceMeta string) string {
	order := assistantGoWireSubcontractOrder(schema)
	var b strings.Builder
	b.WriteString("// Code generated by tools/codegen_app_metadata (assistant wire Go PoC) from ")
	b.WriteString(sourceMeta)
	b.WriteString(". DO NOT EDIT.\n\n")
	b.WriteString("package ")
	b.WriteString(pkg)
	b.WriteString("\n\n")
	if assistantGoWireNeedsJSON(schema) {
		b.WriteString("import \"encoding/json\"\n\n")
	}

	for _, key := range order {
		sub := schema.Subcontracts[key]
		if strings.TrimSpace(sub.ClassName) == "" {
			continue
		}
		assistantGoWireEmitStruct(&b, schema, sub.ClassName, sub.Fields)
		b.WriteString("\n")
	}
	root := strings.TrimSpace(schema.DartClass)
	if root != "" {
		assistantGoWireEmitStruct(&b, schema, root, schema.Fields)
	}
	return b.String()
}

func assistantGoWireNeedsJSON(schema *assistantContractSchema) bool {
	var visit func(fields []assistantContractField) bool
	visit = func(fields []assistantContractField) bool {
		for _, field := range fields {
			switch strings.TrimSpace(field.Type) {
			case "map", "partitioned_map", "any", "list<map>", "map<object>":
				return true
			case "object":
				ref := strings.TrimSpace(field.Ref)
				if ref == "" {
					return true
				}
				if _, ok := schema.Subcontracts[ref]; !ok {
					return true
				}
			case "list<object>":
				ref := strings.TrimSpace(field.Ref)
				if ref == "" {
					return true
				}
				if _, ok := schema.Subcontracts[ref]; !ok {
					return true
				}
			}
		}
		return false
	}
	if visit(schema.Fields) {
		return true
	}
	for _, sub := range schema.Subcontracts {
		if visit(sub.Fields) {
			return true
		}
	}
	return false
}

func renderAssistantGoWireRoundTripTest(pkg string, schema *assistantContractSchema, fixture string) string {
	root := strings.TrimSpace(schema.DartClass)
	var b strings.Builder
	b.WriteString("package ")
	b.WriteString(pkg)
	b.WriteString("\n\n")
	b.WriteString("import (\n")
	b.WriteString("\t\"encoding/json\"\n")
	b.WriteString("\t\"os\"\n")
	b.WriteString("\t\"path/filepath\"\n")
	b.WriteString("\t\"testing\"\n")
	b.WriteString(")\n\n")
	fmt.Fprintf(&b, "func Test%sWireJSONRoundTrip(t *testing.T) {\n", root)
	b.WriteString("\tt.Parallel()\n")
	b.WriteString("\troot := findServiceRoot(t)\n")
	fmt.Fprintf(&b, "\tp := filepath.Join(root, \"contracts\", \"metadata\", \"assistant\", \"test_fixtures\", %q)\n", fixture)
	b.WriteString("\traw, err := os.ReadFile(p)\n")
	b.WriteString("\tif err != nil {\n")
	b.WriteString("\t\tt.Fatalf(\"read fixture: %v\", err)\n")
	b.WriteString("\t}\n")
	fmt.Fprintf(&b, "\tvar v %s\n", root)
	b.WriteString("\tif err := json.Unmarshal(raw, &v); err != nil {\n")
	b.WriteString("\t\tt.Fatalf(\"unmarshal: %v\", err)\n")
	b.WriteString("\t}\n")
	b.WriteString("\tout, err := json.Marshal(&v)\n")
	b.WriteString("\tif err != nil {\n")
	b.WriteString("\t\tt.Fatalf(\"marshal: %v\", err)\n")
	b.WriteString("\t}\n")
	fmt.Fprintf(&b, "\tvar v2 %s\n", root)
	b.WriteString("\tif err := json.Unmarshal(out, &v2); err != nil {\n")
	b.WriteString("\t\tt.Fatalf(\"roundtrip unmarshal: %v\", err)\n")
	b.WriteString("\t}\n")
	b.WriteString("}\n\n")
	b.WriteString("func findServiceRoot(t *testing.T) string {\n")
	b.WriteString("\tt.Helper()\n")
	b.WriteString("\tdir, err := os.Getwd()\n")
	b.WriteString("\tif err != nil {\n")
	b.WriteString("\t\tt.Fatalf(\"getwd: %v\", err)\n")
	b.WriteString("\t}\n")
	b.WriteString("\tfor d := dir; d != \"/\" && d != \".\"; d = filepath.Dir(d) {\n")
	b.WriteString("\t\tif _, err := os.Stat(filepath.Join(d, \"go.mod\")); err == nil {\n")
	b.WriteString("\t\t\treturn d\n")
	b.WriteString("\t\t}\n")
	b.WriteString("\t}\n")
	b.WriteString("\tt.Fatal(\"go.mod not found from cwd\")\n")
	b.WriteString("\treturn \"\"\n")
	b.WriteString("}\n")
	return b.String()
}

func assistantGoWireSubcontractOrder(schema *assistantContractSchema) []string {
	keys := make([]string, 0, len(schema.Subcontracts))
	for k := range schema.Subcontracts {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	seen := map[string]bool{}
	var order []string
	var visit func(k string)
	visit = func(k string) {
		if seen[k] {
			return
		}
		seen[k] = true
		sub, ok := schema.Subcontracts[k]
		if !ok {
			return
		}
		for _, f := range sub.Fields {
			ref := strings.TrimSpace(f.Ref)
			if _, ok := schema.Subcontracts[ref]; ok {
				visit(ref)
			}
		}
		order = append(order, k)
	}
	for _, k := range keys {
		visit(k)
	}
	return order
}

func assistantGoWireEmitStruct(b *strings.Builder, schema *assistantContractSchema, goName string, fields []assistantContractField) {
	fmt.Fprintf(b, "type %s struct {\n", goName)
	for _, f := range fields {
		jsonTag := f.Name
		goField := assistantGoExportIdent(f.Name)
		goTyp, omit := assistantGoWireFieldType(schema, f)
		tag := jsonTag
		if omit {
			tag = jsonTag + ",omitempty"
		}
		fmt.Fprintf(b, "\t%s %s `json:\"%s\"`\n", goField, goTyp, tag)
	}
	b.WriteString("}\n")
}

func assistantGoExportIdent(name string) string {
	if name == "" {
		return name
	}
	// Minimal camelCase → exported Go: upper first rune only (PoC).
	r := []rune(name)
	if len(r) == 0 {
		return name
	}
	return strings.ToUpper(string(r[0])) + string(r[1:])
}

func assistantGoWireFieldType(schema *assistantContractSchema, f assistantContractField) (typ string, omitempty bool) {
	opt := assistantGoWireOptional(f)
	switch strings.TrimSpace(f.Type) {
	case "string":
		if opt {
			return "*string", true
		}
		return "string", false
	case "int":
		if opt {
			return "*int", true
		}
		return "int", false
	case "double":
		if opt {
			return "*float64", true
		}
		return "float64", false
	case "bool":
		return "bool", false
	case "enum":
		if opt {
			return "*string", true
		}
		return "string", false
	case "map", "partitioned_map", "any":
		return "json.RawMessage", opt
	case "list<string>":
		return "[]string", opt
	case "list<map>":
		return "[]json.RawMessage", opt
	case "object":
		ref := strings.TrimSpace(f.Ref)
		if ref != "" {
			if sub, ok := schema.Subcontracts[ref]; ok && strings.TrimSpace(sub.ClassName) != "" {
				cn := strings.TrimSpace(sub.ClassName)
				return "*" + cn, opt
			}
		}
		return "json.RawMessage", opt
	case "list<object>":
		ref := strings.TrimSpace(f.Ref)
		if ref != "" {
			if sub, ok := schema.Subcontracts[ref]; ok && strings.TrimSpace(sub.ClassName) != "" {
				return "[]" + strings.TrimSpace(sub.ClassName), opt
			}
		}
		return "[]json.RawMessage", opt
	case "map<object>":
		return "json.RawMessage", opt
	default:
		return "json.RawMessage", true
	}
}

func assistantGoWireOptional(f assistantContractField) bool {
	if f.Required {
		return false
	}
	return f.Default == nil
}
