package main

import (
	"fmt"
	"path/filepath"
	"sort"
	"strings"
)

// renderCircleWriteWireWritableKeysDart 从 social/circle/service.yaml 的 writable_fields 生成
// App 端 Wire DTO 用的键集合（与手写 _writableKeys 双写收口）。
func renderCircleWriteWireWritableKeysDart(metadataDir string) (string, error) {
	svcPath := filepath.Join(metadataDir, "social", "circle", "service.yaml")
	sf, err := readService(svcPath)
	if err != nil {
		return "", fmt.Errorf("read circle service.yaml: %w", err)
	}
	create := findWritableFields(sf.APIRoutes, "CreateCircle")
	if len(create) == 0 {
		return "", fmt.Errorf("circle service: CreateCircle writable_fields missing")
	}
	group := findWritableFields(sf.APIRoutes, "CreateCircleGroup")
	if len(group) == 0 {
		return "", fmt.Errorf("circle service: CreateCircleGroup writable_fields missing")
	}
	groupSet := make(map[string]struct{}, len(group)+2)
	for _, k := range group {
		groupSet[k] = struct{}{}
	}
	// fromMap 吞掉 wire 里的 id 别名，避免落入 extra（非云契约 writable_fields）
	for _, k := range []string{"id", "_id"} {
		groupSet[k] = struct{}{}
	}
	groupKeys := make([]string, 0, len(groupSet))
	for k := range groupSet {
		groupKeys = append(groupKeys, k)
	}
	sort.Strings(groupKeys)

	createSorted := append([]string(nil), create...)
	sort.Strings(createSorted)

	var b strings.Builder
	b.WriteString("// Code generated by tools/codegen_app_metadata from social/circle/service.yaml api_routes writable_fields. DO NOT EDIT.\n")
	b.WriteString("// ignore_for_file: dangling_library_doc_comments\n\n")
	b.WriteString("/// CreateCircle / CreateCircleGroup 可写字段键，与 metadata SSOT 对齐。\n")
	b.WriteString("library;\n\n")
	b.WriteString("// ignore: avoid_classes_with_only_static_members\n")
	b.WriteString("abstract final class CircleWriteWireWritableKeys {\n")
	b.WriteString("  CircleWriteWireWritableKeys._();\n\n")
	writeSet := func(name string, keys []string) {
		b.WriteString(fmt.Sprintf("  static const Set<String> %s = {\n", name))
		for _, k := range keys {
			b.WriteString(fmt.Sprintf("    '%s',\n", k))
		}
		b.WriteString("  };\n\n")
	}
	writeList := func(name string, keys []string) {
		b.WriteString(fmt.Sprintf(
			"  /// service.yaml 中 `%s` 声明顺序（与对应 Set 成员一致，用于序列化/校验对照）。\n",
			name,
		))
		b.WriteString(fmt.Sprintf("  static const List<String> %s = [\n", name))
		for _, k := range keys {
			b.WriteString(fmt.Sprintf("    '%s',\n", k))
		}
		b.WriteString("  ];\n\n")
	}
	writeSet("createCircle", createSorted)
	writeList("createCircleServiceFieldOrder", create)
	writeSet("createCircleGroup", groupKeys)
	groupOrder := append([]string(nil), group...)
	for _, extra := range []string{"id", "_id"} {
		found := false
		for _, x := range group {
			if x == extra {
				found = true
				break
			}
		}
		if !found {
			groupOrder = append(groupOrder, extra)
		}
	}
	writeList("createCircleGroupServiceFieldOrder", groupOrder)
	b.WriteString("}\n")
	return b.String(), nil
}
