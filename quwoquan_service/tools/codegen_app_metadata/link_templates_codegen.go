// link_templates.yaml

type linkTemplatesRuntimeBinding struct {
	DartDefineKey   string `yaml:"dart_define_key"`
	RemoteConfigKey string `yaml:"remote_config_key"`
}

type linkTemplatesQueryRule struct {
	When         string `yaml:"when"`
	AppendQuery  string `yaml:"append_query"`
}

type linkTemplatesAppDeepLink struct {
	Scheme       string                   `yaml:"scheme"`
	Host         string                   `yaml:"host"`
	PathTemplate string                   `yaml:"path_template"`
	QueryRules   []linkTemplatesQueryRule `yaml:"query_rules"`
}

type linkTemplatesWeb struct {
	PathTemplate string `yaml:"path_template"`
}

type linkTemplatesNavigation struct {
	RouteID       string            `yaml:"route_id"`
	ParamBindings map[string]string `yaml:"param_bindings"`
}

type linkTemplatesEntity struct {
	Description   string                   `yaml:"description"`
	PrimaryParams []string                 `yaml:"primary_params"`
	AppDeepLink   linkTemplatesAppDeepLink `yaml:"app_deep_link"`
	Web           linkTemplatesWeb         `yaml:"web"`
	Navigation    linkTemplatesNavigation  `yaml:"navigation"`
}

type linkTemplatesFile struct {
	Version              int                         `yaml:"version"`
	RuntimeOriginBinding linkTemplatesRuntimeBinding `yaml:"runtime_origin_binding"`
	Entities             map[string]linkTemplatesEntity `yaml:"entities"`
}

func readLinkTemplates(path string) (*linkTemplatesFile, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var parsed linkTemplatesFile
	if err := yaml.Unmarshal(data, &parsed); err != nil {
		return nil, err
	}
	return &parsed, nil
}

func validateLinkTemplates(ar *appRoutesFile, lt *linkTemplatesFile) error {
	if ar == nil || lt == nil {
		return fmt.Errorf("validateLinkTemplates: app routes and link templates required")
	}
	routeByID := map[string]appRouteDef{}
	for _, r := range ar.Routes {
		if strings.TrimSpace(r.ID) != "" {
			routeByID[r.ID] = r
		}
	}
	for key, ent := range lt.Entities {
		rid := strings.TrimSpace(ent.Navigation.RouteID)
		if rid == "" {
			return fmt.Errorf("link_templates entity %q: empty navigation.route_id", key)
		}
		route, ok := routeByID[rid]
		if !ok {
			return fmt.Errorf("link_templates entity %q: route_id %q not found in app_routes.yaml", key, rid)
		}
		pathParams := extractPathParams(route.Path)
		paramSet := map[string]bool{}
		for _, p := range pathParams {
			paramSet[p] = true
		}
		for linkName, routeParam := range ent.Navigation.ParamBindings {
			if !paramSet[routeParam] {
				return fmt.Errorf("link_templates entity %q: param_bindings %q -> %q: route %q path params are %v",
					key, linkName, routeParam, route.Path, pathParams)
			}
		}
	}
	return nil
}

func renderLinkTemplatesDart(lt *linkTemplatesFile) string {
	var b strings.Builder
	b.WriteString("// Code generated by tools/codegen_app_metadata from _shared/link_templates.yaml. DO NOT EDIT.\n\n")
	b.WriteString("// ignore: avoid_classes_with_only_static_members\n")
	b.WriteString("class AppLinkTemplates {\n")
	b.WriteString("  const AppLinkTemplates._();\n\n")

	dd := strings.TrimSpace(lt.RuntimeOriginBinding.DartDefineKey)
	if dd == "" {
		dd = "PUBLIC_WEB_BASE_URL"
	}
	rc := strings.TrimSpace(lt.RuntimeOriginBinding.RemoteConfigKey)
	if rc == "" {
		rc = "public_web_base_url"
	}
	b.WriteString(fmt.Sprintf("  static const String publicWebDartDefineKey = '%s';\n", escapeDartString(dd)))
	b.WriteString(fmt.Sprintf("  static const String publicWebRemoteConfigKey = '%s';\n\n", escapeDartString(rc)))

	keys := make([]string, 0, len(lt.Entities))
	for k := range lt.Entities {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	for _, entityKey := range keys {
		ent := lt.Entities[entityKey]
		prefix := toDartValueName(entityKey)
		rid := strings.TrimSpace(ent.Navigation.RouteID)
		b.WriteString(fmt.Sprintf("  static const String %sRouteId = '%s';\n", prefix, escapeDartString(rid)))
		if len(ent.Navigation.ParamBindings) > 0 {
			b.WriteString(fmt.Sprintf("  static const Map<String, String> %sNavigationParamBindings = <String, String>{\n", prefix))
			linkNames := make([]string, 0, len(ent.Navigation.ParamBindings))
			for ln := range ent.Navigation.ParamBindings {
				linkNames = append(linkNames, ln)
			}
			sort.Strings(linkNames)
			for _, ln := range linkNames {
				rv := ent.Navigation.ParamBindings[ln]
				b.WriteString(fmt.Sprintf("    '%s': '%s',\n", escapeDartString(ln), escapeDartString(rv)))
			}
			b.WriteString("  };\n")
		}
		b.WriteString("\n")

		// Web path
		webTpl := strings.TrimSpace(ent.Web.PathTemplate)
		b.WriteString(renderWebPathMethod(prefix, webTpl, ent.PrimaryParams))

		// App deep link
		b.WriteString(renderAppDeepLinkMethod(prefix, ent))
		b.WriteString("\n")
	}

	b.WriteString("  static String _fillPathTemplate(String template, Map<String, String> params) {\n")
	b.WriteString("    var out = template;\n")
	b.WriteString("    params.forEach((key, value) {\n")
	b.WriteString(`      out = out.replaceAll('{$key}', Uri.encodeComponent(value));` + "\n")
	b.WriteString("    });\n")
	b.WriteString("    return out;\n")
	b.WriteString("  }\n")
	b.WriteString("}\n")
	return b.String()
}

// linkTemplateArgName maps YAML path keys like postId → Dart identifier postId (not postid).
func linkTemplateArgName(param string) string {
	param = strings.TrimSpace(param)
	if param == "" {
		return "param"
	}
	r := []rune(param)
	r[0] = unicode.ToLower(r[0])
	return string(r)
}

func renderWebPathMethod(prefix, webTpl string, primaryParams []string) string {
	var b strings.Builder
	method := prefix + "WebPath"
	args := formatDartMethodParams(primaryParams, nil)
	b.WriteString(fmt.Sprintf("  static String %s(%s) {\n", method, args))
	for _, p := range primaryParams {
		pn := strings.TrimSpace(p)
		if pn == "" {
			continue
		}
		argName := linkTemplateArgName(pn)
		b.WriteString(fmt.Sprintf("    final %sNorm = %s.trim();\n", argName, argName))
		b.WriteString(fmt.Sprintf("    if (%sNorm.isEmpty) return '';\n", argName))
	}
	b.WriteString(fmt.Sprintf("    return _fillPathTemplate('%s', <String, String>{\n", escapeDartString(webTpl)))
	for _, p := range primaryParams {
		pn := strings.TrimSpace(p)
		if pn == "" {
			continue
		}
		argName := linkTemplateArgName(pn)
		b.WriteString(fmt.Sprintf("      '%s': %sNorm,\n", escapeDartString(pn), argName))
	}
	b.WriteString("    });\n")
	b.WriteString("  }\n\n")
	return b.String()
}

// formatDartMethodParams builds Dart formal parameters: positional strings, then optional named bools in {}.
func formatDartMethodParams(primaryParams []string, extraBoolParams []string) string {
	pos := make([]string, 0)
	for _, p := range primaryParams {
		pn := strings.TrimSpace(p)
		if pn == "" {
			continue
		}
		pos = append(pos, fmt.Sprintf("String %s", linkTemplateArgName(pn)))
	}
	positional := strings.Join(pos, ", ")
	if len(extraBoolParams) == 0 {
		return positional
	}
	named := make([]string, 0, len(extraBoolParams))
	for _, e := range extraBoolParams {
		named = append(named, fmt.Sprintf("bool %s = false", e))
	}
	if positional == "" {
		return "{" + strings.Join(named, ", ") + "}"
	}
	return positional + ", {" + strings.Join(named, ", ") + "}"
}

func renderAppDeepLinkMethod(prefix string, ent linkTemplatesEntity) string {
	var b strings.Builder
	method := prefix + "AppDeepLink"
	dl := ent.AppDeepLink
	scheme := strings.TrimSpace(dl.Scheme)
	host := strings.TrimSpace(dl.Host)
	pathTpl := strings.TrimSpace(dl.PathTemplate)

	var extraBool []string
	for _, rule := range dl.QueryRules {
		when := strings.TrimSpace(rule.When)
		if when == "" {
			continue
		}
		extraBool = append(extraBool, queryRuleToDartParam(when))
	}

	args := formatDartMethodParams(ent.PrimaryParams, extraBool)
	b.WriteString(fmt.Sprintf("  static String %s(%s) {\n", method, args))

	for _, p := range ent.PrimaryParams {
		pn := strings.TrimSpace(p)
		if pn == "" {
			continue
		}
		argName := linkTemplateArgName(pn)
		b.WriteString(fmt.Sprintf("    final %sNorm = %s.trim();\n", argName, argName))
		b.WriteString(fmt.Sprintf("    if (%sNorm.isEmpty) return '';\n", argName))
	}

	b.WriteString(fmt.Sprintf("    final path = _fillPathTemplate('%s', <String, String>{\n", escapeDartString(pathTpl)))
	for _, p := range ent.PrimaryParams {
		pn := strings.TrimSpace(p)
		if pn == "" {
			continue
		}
		argName := linkTemplateArgName(pn)
		b.WriteString(fmt.Sprintf("      '%s': %sNorm,\n", escapeDartString(pn), argName))
	}
	b.WriteString("    });\n")

	if len(dl.QueryRules) == 0 {
		b.WriteString(fmt.Sprintf("    return Uri(scheme: '%s', host: '%s', path: '/%s').toString();\n",
			escapeDartString(scheme), escapeDartString(host), "$path"))
	} else {
		b.WriteString("    final qp = <String, String>{};\n")
		for _, rule := range dl.QueryRules {
			when := strings.TrimSpace(rule.When)
			q := strings.TrimSpace(rule.AppendQuery)
			if when == "" || q == "" {
				continue
			}
			k, v := parseAppendQuery(q)
			if k == "" {
				continue
			}
			param := queryRuleToDartParam(when)
			b.WriteString(fmt.Sprintf("    if (%s) qp['%s'] = '%s';\n", param, escapeDartString(k), escapeDartString(v)))
		}
		b.WriteString(fmt.Sprintf("    return Uri(\n      scheme: '%s',\n      host: '%s',\n      path: '/%s',\n      queryParameters: qp.isEmpty ? null : qp,\n    ).toString();\n",
			escapeDartString(scheme), escapeDartString(host), "$path"))
	}
	b.WriteString("  }\n\n")
	return b.String()
}

func queryRuleToDartParam(when string) string {
	return toDartValueName(when)
}

func parseAppendQuery(s string) (key, val string) {
	idx := strings.IndexByte(s, '=')
	if idx < 0 {
		return strings.TrimSpace(s), ""
	}
	return strings.TrimSpace(s[:idx]), strings.TrimSpace(s[idx+1:])
}
