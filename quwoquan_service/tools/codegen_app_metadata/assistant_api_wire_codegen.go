// generateAssistantCloudApiWireDart emits strongly-typed AssistantRepository wire views from
// contracts/metadata/assistant/assistant_run/fields.yaml (subset aligned with service.yaml routes).
func generateAssistantCloudApiWireDart(metadataDir, appDir string) error {
	path := filepath.Join(metadataDir, "assistant", "assistant_run", "fields.yaml")
	ff, err := readFields(path)
	if err != nil {
		return err
	}
	names := []string{
		"AssistantSearchCitationView",
		"AssistantSearchResultView",
		"AssistantSearchXiaoquRequestWire",
		"AssistantReportPageContextRequestWire",
		"AssistantUserTaskView",
		"AssistantUserMemoryView",
		"AssistantUserTaskListView",
		"AssistantUserMemoryListView",
		"AssistantSkillCatalogItemView",
		"AssistantSkillCatalogListView",
		"SuggestedAction",
		"SuggestedActionListView",
		"PageContextAck",
		"InteractionEvent",
		"Scorecard",
		"AssistantPolicyView",
		"AssistantInteractionReportBatchAck",
		"AssistantScorecardReportBatchAck",
		"AssistantLearningOpsSummaryView",
	}
	out := renderAssistantCloudApiWireDart(ff, names)
	outPath := filepath.Join(appDir, "lib", "cloud", "runtime", "generated", "assistant", "assistant_cloud_api_wire.g.dart")
	if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
		return err
	}
	writeFile(outPath, out)
	return nil
}

func renderAssistantCloudApiWireDart(ff *fieldsFile, entityNames []string) string {
	deps := map[string]map[string]bool{}
	for _, n := range entityNames {
		deps[n] = map[string]bool{}
		ent, ok := ff.Entities[n]
		if !ok {
			continue
		}
		for _, f := range ent.Fields {
			inner, ok := assistantWireListElementType(f.Type)
			if ok && containsString(entityNames, inner) {
				deps[n][inner] = true
			}
		}
	}
	order := assistantWireTopoEntityOrder(entityNames, deps)

	var b strings.Builder
	b.WriteString("// Code generated by tools/codegen_app_metadata from assistant/assistant_run/fields.yaml. DO NOT EDIT.\n")
	b.WriteString("// ignore_for_file: prefer_const_constructors, unnecessary_null_in_if_null_operators\n\n")

	for _, name := range order {
		ent, ok := ff.Entities[name]
		if !ok {
			continue
		}
		assistantWireEmitEntityDart(&b, ff, name, ent)
		b.WriteString("\n")
	}

	return b.String()
}

func containsString(slice []string, s string) bool {
	for _, x := range slice {
		if x == s {
			return true
		}
	}
	return false
}

var assistantWireListTypeRe = regexp.MustCompile(`^\[\](.+)$`)

func assistantWireListElementType(t string) (string, bool) {
	t = strings.TrimSpace(t)
	m := assistantWireListTypeRe.FindStringSubmatch(t)
	if m == nil {
		return "", false
	}
	return strings.TrimSpace(m[1]), true
}

func assistantWireTopoEntityOrder(names []string, deps map[string]map[string]bool) []string {
	seen := map[string]bool{}
	var order []string
	var visit func(n string)
	visit = func(n string) {
		if seen[n] {
			return
		}
		seen[n] = true
		for d := range deps[n] {
			if containsString(names, d) {
				visit(d)
			}
		}
		order = append(order, n)
	}
	for _, n := range names {
		visit(n)
	}
	return order
}

func assistantWireFieldNullable(f fieldDef) bool {
	for _, c := range f.Constraints {
		if c == "NOT_NULL" || c == "PK" {
			return false
		}
	}
	for _, c := range f.Constraints {
		if c == "NULLABLE" {
			return true
		}
	}
	return true
}

func assistantWireDartType(ff *fieldsFile, t string, nullable bool) string {
	t = strings.TrimSpace(t)
	if inner, ok := assistantWireListElementType(t); ok {
		if _, isEnt := ff.Entities[inner]; isEnt {
			if nullable {
				return "List<" + inner + ">?"
			}
			return "List<" + inner + ">"
		}
		switch inner {
		case "string":
			if nullable {
				return "List<String>?"
			}
			return "List<String>"
		case "object":
			if nullable {
				return "List<Map<String, dynamic>>?"
			}
			return "List<Map<String, dynamic>>"
		default:
			if nullable {
				return "List<dynamic>?"
			}
			return "List<dynamic>"
		}
	}
	switch t {
	case "string":
		if nullable {
			return "String?"
		}
		return "String"
	case "bool":
		if nullable {
			return "bool?"
		}
		return "bool"
	case "int", "int64":
		if nullable {
			return "int?"
		}
		return "int"
	case "float":
		if nullable {
			return "double?"
		}
		return "double"
	case "timestamp":
		return "String?"
	case "object", "jsonb":
		if nullable {
			return "Map<String, dynamic>?"
		}
		return "Map<String, dynamic>"
	case "enum":
		return "String?"
	default:
		return "dynamic"
	}
}

func assistantWireEmitEntityDart(b *strings.Builder, ff *fieldsFile, name string, ent entityDef) {
	fmt.Fprintf(b, "class %s {\n", name)
	fmt.Fprintf(b, "  const %s({\n", name)
	for _, f := range ent.Fields {
		nul := assistantWireFieldNullable(f)
		if nul {
			if inner, ok := assistantWireListElementType(f.Type); ok {
				if _, isEnt := ff.Entities[inner]; isEnt {
					fmt.Fprintf(b, "    this.%s = const [],\n", f.Name)
					continue
				}
				if inner == "string" {
					fmt.Fprintf(b, "    this.%s = const [],\n", f.Name)
					continue
				}
				if inner == "object" {
					if !nul {
						fmt.Fprintf(b, "    this.%s = const [],\n", f.Name)
					} else {
						fmt.Fprintf(b, "    this.%s,\n", f.Name)
					}
					continue
				}
			}
			fmt.Fprintf(b, "    this.%s,\n", f.Name)
		} else {
			fmt.Fprintf(b, "    required this.%s,\n", f.Name)
		}
	}
	b.WriteString("  });\n\n")
	for _, f := range ent.Fields {
		nul := assistantWireFieldNullable(f)
		dt := assistantWireDartType(ff, f.Type, nul)
		fmt.Fprintf(b, "  final %s %s;\n", dt, f.Name)
	}
	b.WriteString("\n")
	fmt.Fprintf(b, "  factory %s.fromJson(Map<String, dynamic> json) {\n", name)
	fmt.Fprintf(b, "    return %s(\n", name)
	for _, f := range ent.Fields {
		fmt.Fprintf(b, "      %s: %s,\n", f.Name, assistantWireFromJsonExpr(ff, name, f))
	}
	b.WriteString("    );\n  }\n\n")
	fmt.Fprintf(b, "  Map<String, dynamic> toJson() => <String, dynamic>{\n")
	for _, f := range ent.Fields {
		fmt.Fprintf(b, "        '%s': %s,\n", f.Name, f.Name)
	}
	b.WriteString("      };\n")

	if name == "AssistantUserTaskView" {
		b.WriteString(`
  /// 兼容旧 UI 使用的 ` + "`title` / `desc`" + ` Map。
  Map<String, dynamic> toScheduleRowMap() => <String, dynamic>{
        'title': title,
        'desc': description ?? '',
      };
`)
	}
	b.WriteString("}\n")
}

func assistantWireFromJsonExpr(ff *fieldsFile, entityName string, f fieldDef) string {
	n := f.Name
	nul := assistantWireFieldNullable(f)
	t := strings.TrimSpace(f.Type)

	if inner, ok := assistantWireListElementType(t); ok {
		if _, isEnt := ff.Entities[inner]; isEnt {
			if entityName == "AssistantSearchResultView" && n == "citations" {
				return `((json['citations'] as List?) ?? const [])
            .whereType<Map>()
            .map((item) => AssistantSearchCitationView.fromJson(item.cast<String, dynamic>()))
            .where((item) => item.citationId.isNotEmpty || item.title.isNotEmpty)
            .toList(growable: false)`
			}
			return fmt.Sprintf(`((json['%s'] as List?) ?? const [])
            .whereType<Map>()
            .map((item) => %s.fromJson(item.cast<String, dynamic>()))
            .toList(growable: false)`, n, inner)
		}
		if inner == "string" {
			return fmt.Sprintf(`((json['%s'] as List?) ?? const [])
            .map((e) => e.toString())
            .toList(growable: false)`, n)
		}
		if inner == "object" {
			if nul {
				return fmt.Sprintf(`(json['%s'] as List?)
            ?.whereType<Map>()
            .map((m) => m.cast<String, dynamic>())
            .toList(growable: false)`, n)
			}
			return fmt.Sprintf(`((json['%s'] as List?) ?? const [])
            .whereType<Map>()
            .map((m) => m.cast<String, dynamic>())
            .toList(growable: false)`, n)
		}
	}

	switch n {
	case "queryEcho":
		return `(json['queryEcho'] ?? json['userQuery'] ?? '').toString().trim()`
	case "taskId":
		return `(json['taskId'] ?? json['task_id'] ?? json['id'] ?? '').toString()`
	case "memoryId":
		return `(json['memoryId'] ?? json['memory_id'] ?? json['id'] ?? '').toString()`
	case "skillId":
		return `(json['skillId'] ?? json['skill_id'] ?? json['id'] ?? '').toString()`
	case "displayName":
		return `(json['displayName'] ?? json['display_name'] ?? json['name'] ?? '').toString()`
	case "description":
		if entityName == "AssistantSkillCatalogItemView" {
			return `json['description']?.toString() ?? json['desc']?.toString()`
		}
	case "requiresConsent":
		if entityName == "AssistantSkillCatalogItemView" {
			return `json['requiresConsent'] == true || json['requires_consent'] == true`
		}
	case "accepted":
		if entityName == "AssistantInteractionReportBatchAck" || entityName == "AssistantScorecardReportBatchAck" {
			return `json['accepted'] != false`
		}
	case "count":
		if entityName == "AssistantInteractionReportBatchAck" || entityName == "AssistantScorecardReportBatchAck" {
			return `(json['count'] as num?)?.toInt() ?? 0`
		}
	case "resource":
		if entityName == "AssistantInteractionReportBatchAck" || entityName == "AssistantScorecardReportBatchAck" {
			return `(json['resource'] ?? '').toString()`
		}
	case "status":
		if entityName == "AssistantUserTaskView" {
			return `(json['status'] ?? 'pending').toString()`
		}
	case "sourceType":
		if entityName == "AssistantUserMemoryView" {
			return `json['sourceType']?.toString() ?? json['source_type']?.toString()`
		}
	case "createdAt":
		if entityName == "AssistantUserMemoryView" {
			return `json['createdAt']?.toString() ?? json['created_at']?.toString()`
		}
	case "updatedAt":
		if entityName == "AssistantUserMemoryView" || entityName == "AssistantUserTaskView" {
			return `json['updatedAt']?.toString() ?? json['updated_at']?.toString()`
		}
	case "dueAt":
		if entityName == "AssistantUserTaskView" {
			return `json['dueAt']?.toString() ?? json['due_at']?.toString()`
		}
	case "sourceSkillId":
		if entityName == "AssistantUserTaskView" {
			return `json['sourceSkillId']?.toString() ?? json['source_skill_id']?.toString()`
		}
	case "iconHint":
		if entityName == "AssistantSkillCatalogItemView" {
			return `json['iconHint']?.toString() ?? json['icon_hint']?.toString()`
		}
	}

	switch t {
	case "string":
		if nul {
			return `json['` + n + `']?.toString()`
		}
		return `(json['` + n + `'] ?? '').toString()`
	case "bool":
		if nul {
			return `(json['` + n + `'] as bool?)`
		}
		return `json['` + n + `'] == true`
	case "int", "int64":
		if nul {
			return `(json['` + n + `'] as num?)?.toInt()`
		}
		return `(json['` + n + `'] as num?)?.toInt() ?? 0`
	case "float":
		if nul {
			return `(json['` + n + `'] as num?)?.toDouble()`
		}
		return `(json['` + n + `'] as num?)?.toDouble() ?? 0.0`
	case "timestamp":
		return `json['` + n + `']?.toString() ?? json['` + assistantWireSnake(n) + `']?.toString()`
	case "object", "jsonb":
		if nul {
			return `(json['` + n + `'] as Map?)?.cast<String, dynamic>()`
		}
		return `(json['` + n + `'] as Map?)?.cast<String, dynamic>() ?? const <String, dynamic>{}`
	case "enum":
		return `json['` + n + `']?.toString()`
	default:
		return `json['` + n + `']`
	}
}

func assistantWireSnake(name string) string {
	var b strings.Builder
	for i, r := range name {
		if i > 0 && r >= 'A' && r <= 'Z' {
			b.WriteByte('_')
		}
		if r >= 'A' && r <= 'Z' {
			b.WriteRune(r - 'A' + 'a')
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}
