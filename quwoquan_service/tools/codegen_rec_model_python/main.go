// Codegen for rec-model-service Python: reads service metadata + service projections
// and generates Pydantic models + FastAPI route skeleton. Single source of truth with Go/App.
package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

const (
	genHeader = "# Code generated by tools/codegen_rec_model_python. DO NOT EDIT.\n"
)

func main() {
	metadataDir := flag.String("metadata-dir", "contracts/metadata", "metadata root directory")
	outputDir := flag.String("output-dir", "services/rec-model-service/generated", "Python generated output directory")
	serviceDir := flag.String("service-dir", "rec_model_service", "service metadata directory name")
	flag.Parse()

	if err := run(*metadataDir, *outputDir, *serviceDir); err != nil {
		fmt.Fprintf(os.Stderr, "codegen_rec_model_python: %v\n", err)
		os.Exit(1)
	}
	fmt.Printf("generated Python code at %s\n", *outputDir)
}

func run(metadataDir, outputDir, serviceDir string) error {
	servicePath := filepath.Join(metadataDir, serviceDir)
	projectionsPath := filepath.Join(servicePath, "projections")

	fields, err := loadFields(filepath.Join(servicePath, "fields.yaml"))
	if err != nil {
		return fmt.Errorf("load fields: %w", err)
	}
	service, err := loadService(filepath.Join(servicePath, "service.yaml"))
	if err != nil {
		return fmt.Errorf("load service: %w", err)
	}

	projections, err := loadProjections(projectionsPath)
	if err != nil {
		return fmt.Errorf("load projections: %w", err)
	}

	if err := os.MkdirAll(filepath.Join(outputDir, "models"), 0755); err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Join(outputDir, "api"), 0755); err != nil {
		return err
	}

	reqRespPy := generateRequestResponsePy(fields)
	if err := os.WriteFile(filepath.Join(outputDir, "models", "request_response.py"), []byte(reqRespPy), 0644); err != nil {
		return err
	}

	projPy := generateProjectionsPy(projections)
	if err := os.WriteFile(filepath.Join(outputDir, "models", "projections.py"), []byte(projPy), 0644); err != nil {
		return err
	}

	modelsInit := generateModelsInit()
	if err := os.WriteFile(filepath.Join(outputDir, "models", "__init__.py"), []byte(modelsInit), 0644); err != nil {
		return err
	}

	routesPy := generateRoutesPy(service)
	if err := os.WriteFile(filepath.Join(outputDir, "api", "routes.py"), []byte(routesPy), 0644); err != nil {
		return err
	}

	schemasPy := generateSchemasPy(fields)
	if err := os.WriteFile(filepath.Join(outputDir, "api", "schemas.py"), []byte(schemasPy), 0644); err != nil {
		return err
	}

	apiInit := generateAPIInit()
	if err := os.WriteFile(filepath.Join(outputDir, "api", "__init__.py"), []byte(apiInit), 0644); err != nil {
		return err
	}

	rootInit := generateRootInit()
	if err := os.WriteFile(filepath.Join(outputDir, "__init__.py"), []byte(rootInit), 0644); err != nil {
		return err
	}

	// Generate content features + training sample from content/post/behaviors.yaml
	behaviorsPath := resolveContentBehaviors(metadataDir)
	if behaviorsPath != "" {
		beh, err := loadBehaviors(behaviorsPath)
		if err == nil && beh != nil {
			if beh.RecommendFeatures.PythonClass != "" {
				featuresPy := generateContentFeaturesPy(beh)
				featsPath := filepath.Join(outputDir, "content_features.py")
				if err := os.WriteFile(featsPath, []byte(featuresPy), 0644); err != nil {
					return err
				}
				fmt.Printf("generated: %s\n", featsPath)
			}
			if beh.TrainingSample.PythonClass != "" {
				samplePy := generateTrainingSamplePy(beh)
				samplePath := filepath.Join(outputDir, "training_sample.py")
				if err := os.WriteFile(samplePath, []byte(samplePy), 0644); err != nil {
					return err
				}
				fmt.Printf("generated: %s\n", samplePath)
			}
		}
	}

	return nil
}

func resolveContentBehaviors(metadataDir string) string {
	// Domain-centric path first
	p := filepath.Join(metadataDir, "content", "post", "behaviors.yaml")
	if _, err := os.Stat(p); err == nil {
		return p
	}
	p = filepath.Join(metadataDir, "post", "behaviors.yaml")
	if _, err := os.Stat(p); err == nil {
		return p
	}
	return ""
}

// --- YAML structs ---

type fieldsFile struct {
	Entities map[string]entityDef `yaml:"entities"`
}

type entityDef struct {
	Description string     `yaml:"description"`
	Fields      []fieldDef `yaml:"fields"`
}

type fieldDef struct {
	Name        string   `yaml:"name"`
	Type        string   `yaml:"type"`
	Constraints []string `yaml:"constraints"`
	Description string   `yaml:"description"`
}

type serviceFile struct {
	APIRoutes []routeDef `yaml:"api_routes"`
}

type routeDef struct {
	Method          string `yaml:"method"`
	Path            string `yaml:"path"`
	Operation       string `yaml:"operation"`
	RequestEntity   string `yaml:"request_entity"`
	ResponseEntity  string `yaml:"response_entity"`
	Description     string `yaml:"description"`
}

type projectionFile struct {
	ReadModel string     `yaml:"read_model"`
	Collection string   `yaml:"collection"`
	Fields   []projField `yaml:"fields"`
}

type projField struct {
	Name        string `yaml:"name"`
	Type        string `yaml:"type"`
	Description string `yaml:"description"`
}

type projectionSpec struct {
	ReadModel  string
	Collection string
	Fields    []projField
}

func loadFields(path string) (*fieldsFile, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var f fieldsFile
	if err := yaml.Unmarshal(data, &f); err != nil {
		return nil, err
	}
	return &f, nil
}

func loadService(path string) (*serviceFile, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var s serviceFile
	if err := yaml.Unmarshal(data, &s); err != nil {
		return nil, err
	}
	return &s, nil
}

func loadProjections(projectionsPath string) ([]projectionSpec, error) {
	names := []string{"learning_events.yaml", "training_samples.yaml", "model_registry.yaml"}
	var out []projectionSpec
	for _, n := range names {
		path := filepath.Join(projectionsPath, n)
		data, err := os.ReadFile(path)
		if err != nil {
			if os.IsNotExist(err) {
				continue
			}
			return nil, err
		}
		var p projectionFile
		if err := yaml.Unmarshal(data, &p); err != nil {
			return nil, fmt.Errorf("%s: %w", n, err)
		}
		out = append(out, projectionSpec{
			ReadModel:  p.ReadModel,
			Collection: p.Collection,
			Fields:     p.Fields,
		})
	}
	return out, nil
}

// --- Type mapping metadata -> Python ---

func pyType(metaType string, required bool, entityNames map[string]bool) string {
	t := strings.TrimSpace(metaType)
	optional := !required
	wrapOptional := func(s string) string {
		if optional {
			return s + " | None"
		}
		return s
	}
	switch {
	case t == "string":
		return wrapOptional("str")
	case t == "float64" || t == "number":
		return wrapOptional("float")
	case t == "int64" || t == "int":
		return wrapOptional("int")
	case t == "bool":
		return wrapOptional("bool")
	case t == "object":
		return wrapOptional("dict[str, Any]")
	case t == "timestamp":
		return wrapOptional("float")
	case t == "ObjectId":
		return wrapOptional("str")
	case strings.HasPrefix(t, "[]"):
		inner := strings.TrimPrefix(t, "[]")
		inner = strings.Trim(inner, "[]\"'")
		innerPy := pyType(inner, true, entityNames)
		return wrapOptional("list[" + innerPy + "]")
	default:
		if entityNames[t] {
			return wrapOptional(t)
		}
		return wrapOptional("dict[str, Any]")
	}
}

func isRequired(constraints []string) bool {
	for _, c := range constraints {
		if c == "NOT_NULL" {
			return true
		}
	}
	return false
}

// --- Request/Response generation (entity order: dependencies first) ---

var requestResponseOrder = []string{"CandidateInput", "CandidateScore", "ModelScoreRequest", "ModelScoreResponse"}

func generateRequestResponsePy(f *fieldsFile) string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("\nfrom __future__ import annotations\n\n")
	b.WriteString("from typing import Any\n\n")
	b.WriteString("from pydantic import BaseModel, ConfigDict\n\n")

	entityNames := make(map[string]bool)
	for name := range f.Entities {
		entityNames[name] = true
	}

	for _, entityName := range requestResponseOrder {
		ent, ok := f.Entities[entityName]
		if !ok {
			continue
		}
		b.WriteString(fmt.Sprintf("\n\nclass %s(BaseModel):\n", entityName))
		if ent.Description != "" {
			b.WriteString(fmt.Sprintf("    \"\"\"%s\"\"\"\n", strings.ReplaceAll(ent.Description, "\n", " ")))
		}
		for _, fd := range ent.Fields {
			req := isRequired(fd.Constraints)
			pyT := pyType(fd.Type, req, entityNames)
			def := ""
			if !req {
				def = " = None"
			}
			b.WriteString(fmt.Sprintf("    %s: %s%s\n", fd.Name, pyT, def))
		}
	}
	return b.String()
}

func generateProjectionsPy(projections []projectionSpec) string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("\nfrom __future__ import annotations\n\n")
	b.WriteString("from typing import Any\n\n")
	b.WriteString("from pydantic import BaseModel, ConfigDict\n\n")

	for _, p := range projections {
		className := p.ReadModel
		if strings.HasSuffix(className, "s") && len(className) > 1 {
			className = className[:len(className)-1]
		}
		if className == "LearningEvents" {
			className = "LearningEvent"
		}
		if className == "TrainingSamples" {
			className = "TrainingSample"
		}
		if className == "ModelRegistry" {
			className = "ModelRegistryEntry"
		}
		b.WriteString(fmt.Sprintf("\n\nclass %s(BaseModel):\n", className))
		b.WriteString(fmt.Sprintf("    \"\"\"Read model: %s, collection: %s\"\"\"\n", p.ReadModel, p.Collection))
		for _, fd := range p.Fields {
			pyT := pyType(fd.Type, false, nil)
			b.WriteString(fmt.Sprintf("    %s: %s = None\n", fd.Name, pyT))
		}
		b.WriteString("\n    model_config = ConfigDict(populate_by_name=True)\n")
	}
	return b.String()
}

func generateModelsInit() string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("\nfrom .request_response import (\n")
	b.WriteString("    CandidateInput,\n    CandidateScore,\n    ModelScoreRequest,\n    ModelScoreResponse,\n)\n")
	b.WriteString("from .projections import LearningEvent, ModelRegistryEntry, TrainingSample\n")
	return b.String()
}

func generateRoutesPy(s *serviceFile) string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("\nfrom fastapi import APIRouter\n\n")
	b.WriteString("from .schemas import ModelScoreRequest, ModelScoreResponse\n\n")
	b.WriteString("router = APIRouter()\n\n")

	for _, r := range s.APIRoutes {
		method := strings.ToLower(r.Method)
		path := r.Path
		op := r.Operation
		switch {
		case method == "post" && path == "/v1/score":
			b.WriteString("@router.post(\"/v1/score\", response_model=ModelScoreResponse)\n")
			b.WriteString("def score(body: ModelScoreRequest):\n")
			b.WriteString("    \"\"\"Multi-scenario recommendation scoring. Implement by injecting a scorer.\"\"\"\n")
			b.WriteString("    # TODO: delegate to scorer.score(body) and return ModelScoreResponse(scores=...)\n")
			b.WriteString("    return ModelScoreResponse(scores=[])\n\n\n")
		case method == "get" && path == "/health":
			b.WriteString("@router.get(\"/health\")\n")
			b.WriteString("def health():\n")
			b.WriteString("    \"\"\"Health check.\"\"\"\n")
			b.WriteString("    return {\"status\": \"ok\"}\n\n\n")
		default:
			b.WriteString(fmt.Sprintf("# %s %s (%s)\n", r.Method, path, op))
		}
	}
	return b.String()
}

func generateSchemasPy(f *fieldsFile) string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("\n\"\"\"Re-export request/response models for API use.\"\"\"\n\n")
	b.WriteString("from ..models.request_response import (\n")
	names := make([]string, 0, len(f.Entities))
	for n := range f.Entities {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, n := range names {
		b.WriteString(fmt.Sprintf("    %s,\n", n))
	}
	b.WriteString(")\n")
	b.WriteString("\n__all__ = [\n")
	for _, n := range names {
		b.WriteString(fmt.Sprintf("    %q,\n", n))
	}
	b.WriteString("]\n")
	return b.String()
}

func generateAPIInit() string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("\nfrom .routes import router\n\n__all__ = [\"router\"]\n")
	return b.String()
}

func generateRootInit() string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("\n\"\"\"Generated models and API for rec-model-service. DO NOT EDIT.\"\"\"\n\n")
	b.WriteString("# Lazy subpackages so models can be imported without requiring fastapi.\n")
	b.WriteString("__all__ = [\"api\", \"models\"]\n")
	return b.String()
}

// ── content behaviors/features codegen ───────────────────────────────────────

type featureDef struct {
	Name        string `yaml:"name"`
	Type        string `yaml:"type"`
	PythonType  string `yaml:"python_type"`
	Nullable    bool   `yaml:"nullable"`
	Description string `yaml:"description"`
}

type recommendFeatures struct {
	FeatureGroup string       `yaml:"feature_group"`
	PythonClass  string       `yaml:"python_class"`
	Features     []featureDef `yaml:"features"`
}

type signalDef struct {
	Type      string `yaml:"type"`
	Condition string `yaml:"condition"`
	Weight    int    `yaml:"weight"`
}

type contextFieldDef struct {
	Name string `yaml:"name"`
	Type string `yaml:"type"`
}

type trainingSampleDef struct {
	PythonClass     string            `yaml:"python_class"`
	LabelField      string            `yaml:"label_field"`
	LabelType       string            `yaml:"label_type"`
	PositiveSignals []signalDef       `yaml:"positive_signals"`
	NegativeSignals []signalDef       `yaml:"negative_signals"`
	ContextFields   []contextFieldDef `yaml:"context_fields"`
	ItemFeaturesRef string            `yaml:"item_features_ref"`
}

type behaviorsYAML struct {
	RecommendFeatures recommendFeatures `yaml:"recommend_features"`
	TrainingSample    trainingSampleDef `yaml:"training_sample"`
}

func loadBehaviors(path string) (*behaviorsYAML, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var b behaviorsYAML
	return &b, yaml.Unmarshal(data, &b)
}

func generateContentFeaturesPy(beh *behaviorsYAML) string {
	var sb strings.Builder
	sb.WriteString(genHeader)
	sb.WriteString("\nfrom __future__ import annotations\n\n")
	sb.WriteString("from typing import Optional\n\n")
	sb.WriteString("from pydantic import BaseModel\n\n\n")

	rf := beh.RecommendFeatures
	sb.WriteString(fmt.Sprintf("class %s(BaseModel):\n", rf.PythonClass))
	sb.WriteString(fmt.Sprintf("    \"\"\"Content item features for recommendation. Feature group: %s.\"\"\"\n\n", rf.FeatureGroup))

	for _, f := range rf.Features {
		pt := f.PythonType
		if pt == "" {
			pt = "str"
		}
		defVal := ""
		if f.Nullable || strings.Contains(pt, "Optional") {
			defVal = " = None"
		}
		comment := ""
		if f.Description != "" {
			comment = fmt.Sprintf("  # %s", f.Description)
		}
		sb.WriteString(fmt.Sprintf("    %s: %s%s%s\n", f.Name, pt, defVal, comment))
	}
	return sb.String()
}

func generateTrainingSamplePy(beh *behaviorsYAML) string {
	var sb strings.Builder
	sb.WriteString(genHeader)
	sb.WriteString("\nfrom __future__ import annotations\n\n")
	sb.WriteString("from typing import Optional\n\n")
	sb.WriteString("from pydantic import BaseModel, ConfigDict, Field\n\n")
	sb.WriteString(fmt.Sprintf("from .content_features import %s\n\n\n", beh.RecommendFeatures.PythonClass))

	ts := beh.TrainingSample
	sb.WriteString(fmt.Sprintf("class %s(BaseModel):\n", ts.PythonClass))
	sb.WriteString("    \"\"\"Training sample for content recommendation model.\"\"\"\n\n")

	// context fields
	for _, cf := range ts.ContextFields {
		pt := cf.Type
		if pt == "" {
			pt = "str"
		}
		sb.WriteString(fmt.Sprintf("    %s: %s\n", cf.Name, pt))
	}

	// item features reference
	if ts.ItemFeaturesRef != "" && beh.RecommendFeatures.PythonClass != "" {
		sb.WriteString(fmt.Sprintf("    item_features: %s\n", beh.RecommendFeatures.PythonClass))
	}

	// label
	labelType := ts.LabelType
	if labelType == "" {
		labelType = "int"
	}
	sb.WriteString(fmt.Sprintf("    %s: %s = Field(..., description=\"1=positive, 0=negative\")\n", ts.LabelField, labelType))

	// signal metadata as class-level docs
	if len(ts.PositiveSignals) > 0 || len(ts.NegativeSignals) > 0 {
		sb.WriteString("\n    model_config = ConfigDict(\n")
		sb.WriteString("        json_schema_extra={\n")
		sb.WriteString("            \"positive_signals\": [\n")
		for _, s := range ts.PositiveSignals {
			sb.WriteString(fmt.Sprintf("                {\"type\": \"%s\", \"weight\": %d},\n", s.Type, s.Weight))
		}
		sb.WriteString("            ],\n")
		sb.WriteString("            \"negative_signals\": [\n")
		for _, s := range ts.NegativeSignals {
			sb.WriteString(fmt.Sprintf("                {\"type\": \"%s\", \"weight\": %d},\n", s.Type, s.Weight))
		}
		sb.WriteString("            ],\n")
		sb.WriteString("        },\n")
		sb.WriteString("    )\n")
	}

	return sb.String()
}
